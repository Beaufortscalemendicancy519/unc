/// Emit fused QKV GEMV decode dispatch.
/// Kernel: fused_qkv_gemv_f16 or fused_qkv_qmv_fast_{q4_0,q8_0}
/// Buffers: out_q(0), A(1), W_q(2), out_k(3), W_k(4), out_v(5), W_v(6), params(7)
fn emit_fused_qkv_decode(
    q_node: &Node, k_node: &Node, v_node: &Node,
    graph: &crate::ir::graph::CompGraph,
    memory_plan: &MemoryPlan,
    act_index: &HashMap<TensorId, usize>,
    quant: &str,
    quant_map: &QuantOffsetMap,
    indent: &str,
) -> String {
    let mut s = String::new();
    let p = &graph.metadata.params;
    let quant_active = matches!(quant, "q8_0" | "q4_0" | "q4_mlx");

    // Resolve output and activation buffers
    let (out_q_buf, out_q_off) = resolve_buffer(q_node.output, graph, memory_plan, act_index, true);
    let (out_k_buf, out_k_off) = resolve_buffer(k_node.output, graph, memory_plan, act_index, true);
    let (out_v_buf, out_v_off) = resolve_buffer(v_node.output, graph, memory_plan, act_index, true);
    let (a_buf, a_off) = resolve_buffer(q_node.inputs[0], graph, memory_plan, act_index, true);

    // Resolve weight buffers — use quant_weight_buf for quantized modes
    let resolve_weight = |node: &Node| -> (String, usize) {
        let w_tid = node.inputs[1];
        let tensor = graph.tensors.get(&w_tid).unwrap();
        if let StorageClass::Weight(w) = &tensor.storage {
            if quant_active {
                let q_off = quant_map.get(&w.byte_offset).copied().unwrap_or(0);
                ("s->quant_weight_buf".to_string(), q_off)
            } else {
                ("s->weight_buf".to_string(), w.byte_offset)
            }
        } else {
            let (b, o) = resolve_buffer(w_tid, graph, memory_plan, act_index, true);
            (b.to_string(), o)
        }
    };

    let (wq_buf, wq_off) = resolve_weight(q_node);
    let (wk_buf, wk_off) = resolve_weight(k_node);
    let (wv_buf, wv_off) = resolve_weight(v_node);

    // Output shapes to get M_q and M_kv
    let m_q = graph.tensors.get(&q_node.output)
        .map(|t| t.shape.max_shape()).unwrap_or_default()
        .first().copied().unwrap_or(p.hidden_size) as u32;
    let m_kv = graph.tensors.get(&k_node.output)
        .map(|t| t.shape.max_shape()).unwrap_or_default()
        .first().copied().unwrap_or(p.num_kv_heads * p.head_dim) as u32;

    // K dimension from weight
    let k_dim = graph.tensors.get(&q_node.inputs[1])
        .map(|t| t.shape.max_shape()).unwrap_or_default()
        .last().copied().unwrap_or(p.hidden_size) as u32;

    let kernel_name = if quant_active {
        format!("fused_qkv_qmv_fast_{}", quant)
    } else {
        "fused_qkv_gemv_f16".to_string()
    };

    // Buffer bindings
    let out_q_off_e = format_offset(&out_q_buf, out_q_off);
    let out_k_off_e = format_offset(&out_k_buf, out_k_off);
    let out_v_off_e = format_offset(&out_v_buf, out_v_off);
    let a_off_e = format_offset(&a_buf, a_off);
    s.push_str(&format!("{indent}[enc setBuffer:{out_q_buf} offset:{out_q_off_e} atIndex:0];\n"));
    s.push_str(&format!("{indent}[enc setBuffer:{a_buf} offset:{a_off_e} atIndex:1];\n"));
    s.push_str(&format!("{indent}[enc setBuffer:{wq_buf} offset:{wq_off} atIndex:2];\n"));
    s.push_str(&format!("{indent}[enc setBuffer:{out_k_buf} offset:{out_k_off_e} atIndex:3];\n"));
    s.push_str(&format!("{indent}[enc setBuffer:{wk_buf} offset:{wk_off} atIndex:4];\n"));
    s.push_str(&format!("{indent}[enc setBuffer:{out_v_buf} offset:{out_v_off_e} atIndex:5];\n"));
    s.push_str(&format!("{indent}[enc setBuffer:{wv_buf} offset:{wv_off} atIndex:6];\n"));

    // Params at buffer(7)
    s.push_str(&format!(
        "{indent}{{ uint32_t _p[8] = {{seq_len, act_stride, {k_dim}, {m_q}, {m_kv}, 0, 0, 0}};\n\
         {indent}  [enc setBytes:_p length:32 atIndex:7]; }}\n"
    ));

    // Dispatch — QMV_BM=16 for quantized (128 threads), GEMV_BM=64 for F16 (256 threads)
    let (bm, tg_threads) = if quant_active { (16u32, 128u32) } else { (64u32, 256u32) };
    let grid_x = (m_q + bm - 1) / bm;
        let pso_var = pso_var_name(&kernel_name);
    s.push_str(&format!("{indent}DISPATCH_BARRIER();\n"));
    s.push_str(&format!("{indent}[enc setComputePipelineState:{pso_var}];\n"));
    s.push_str(&format!("{indent}[enc dispatchThreadgroups:MTLSizeMake({grid_x}, 1, 1) threadsPerThreadgroup:MTLSizeMake({tg_threads}, 1, 1)];\n"));

    s
}

