/// Emit Metal orchestrator source and place it beside the output.
pub fn emit_metal(result: &CompilationResult, out_dir: &Path, unfused: bool) -> anyhow::Result<CompiledArtifact> {
    std::fs::create_dir_all(out_dir)?;

    let metallib_path = out_dir.join("unc_kernels.metallib");
    let orchestrator_path = out_dir.join("unc_forward.m");

    // Copy the compiled metallib into the output directory.
    // Priority: runtime env var > build-time env (set by build.rs)
    let src_metallib = std::env::var("UNC_METALLIB_PATH").ok()
        .or_else(|| option_env!("UNC_METALLIB_PATH").map(String::from));
    if let Some(ref src) = src_metallib {
        let src_path = std::path::Path::new(src);
        if src_path.exists() {
            std::fs::copy(src_path, &metallib_path)
                .with_context(|| format!("copying metallib from {src}"))?;
        } else {
            log::warn!("metallib source not found at {src}");
        }
    } else {
        log::warn!("metallib not found — set UNC_METALLIB_PATH or build with `cargo build --release` first");
    }

    let orchestrator_source = generate_orchestrator(result, unfused);
    std::fs::write(&orchestrator_path, &orchestrator_source)
        .context("writing orchestrator source")?;

    let weight_layout = build_weight_layout(result);

    log::info!("Emitted Metal orchestrator: {}", orchestrator_path.display());

    Ok(CompiledArtifact::Metal {
        metallib_path: metallib_path.to_string_lossy().into_owned(),
        orchestrator_source,
        weight_layout,
    })
}

