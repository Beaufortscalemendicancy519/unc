/// Build TensorId → byte offset in act_buf index.
fn build_activation_index(memory_plan: &MemoryPlan) -> HashMap<TensorId, usize> {
    let mut map = HashMap::new();
    for alloc in &memory_plan.activation_buffers {
        for &tid in &alloc.aliases {
            map.insert(tid, alloc.offset);
        }
    }
    map
}

/// Resolve which Metal buffer and byte offset a tensor lives at.
/// In decode_mode, activation tensors map to split region buffers `s->act_r[N]` at offset 0.
/// In prefill mode, they map to `cur_act` with the full offset.
fn resolve_buffer(
    tid: TensorId,
    graph: &crate::ir::graph::CompGraph,
    memory_plan: &MemoryPlan,
    act_index: &HashMap<TensorId, usize>,
    decode_mode: bool,
) -> (String, usize) {
    let tensor = graph.tensors.get(&tid).expect("tensor not found");
    match &tensor.storage {
        StorageClass::Weight(w) => ("s->weight_buf".into(), w.byte_offset),
        StorageClass::KVCache { layer, role, .. } => {
            let offset = memory_plan.kv_cache.layers.get(*layer).map(|l| match role {
                KVRole::Key => l.key_offset,
                KVRole::Value => l.value_offset,
            }).unwrap_or(0);
            ("s->kv_buf".into(), offset)
        }
        _ => {
            if decode_mode {
                let region_id = memory_plan.decode_tensor_region.get(&tid).copied().unwrap_or(0);
                (format!("s->act_r[{}]", region_id), 0)
            } else {
                let offset = act_index.get(&tid).copied().unwrap_or(0);
                ("cur_act".into(), offset)
            }
        }
    }
}

/// Format a buffer offset expression — wraps with act_off() for activation tensors.
fn format_offset(buf: &str, off: usize) -> String {
    if buf == "cur_act" {
        format!("act_off({off}, seq_len)")
    } else {
        format!("{off}")
    }
}

/// Emit `[enc setBuffer:...]` calls for all inputs/outputs.
///
/// Buffer index convention (must match Metal kernel [[buffer(N)]] declarations):
///   [[buffer(0)]] = output tensor
///   [[buffer(1)]] = input[0]
///   [[buffer(2)]] = input[1]  (if present)
///   [[buffer(3)]] = input[2]  (if present, e.g. V for SDPA)
///   [[buffer(4)]] = params    (uint32_t[8], set via setBytes)
fn emit_buffer_bindings(
    node: &Node,
    graph: &crate::ir::graph::CompGraph,
    memory_plan: &MemoryPlan,
    act_index: &HashMap<TensorId, usize>,
    indent: &str,
) -> String {
    let mut s = String::new();

    let (buf, off) = resolve_buffer(node.output, graph, memory_plan, act_index, false);
    let off_expr = format_offset(&buf, off);
    s.push_str(&format!("{indent}[enc setBuffer:{buf} offset:{off_expr} atIndex:0];\n"));

    for (i, &inp) in node.inputs.iter().enumerate() {
        let (buf, off) = resolve_buffer(inp, graph, memory_plan, act_index, false);
        let off_expr = format_offset(&buf, off);
        s.push_str(&format!("{indent}[enc setBuffer:{buf} offset:{off_expr} atIndex:{}];\n", i + 1));
    }

    s
}

/// Resolve buffer for decode path with split activation regions.
/// Activation tensors map to `s->act_r[region_id]` at offset 0.
fn resolve_buffer_decode(
    tid: TensorId,
    graph: &crate::ir::graph::CompGraph,
    memory_plan: &MemoryPlan,
) -> (String, usize) {
    let tensor = graph.tensors.get(&tid).expect("tensor not found");
    match &tensor.storage {
        StorageClass::Weight(w) => ("s->weight_buf".to_string(), w.byte_offset),
        StorageClass::KVCache { layer, role, .. } => {
            let offset = memory_plan.kv_cache.layers.get(*layer).map(|l| match role {
                KVRole::Key => l.key_offset,
                KVRole::Value => l.value_offset,
            }).unwrap_or(0);
            ("s->kv_buf".to_string(), offset)
        }
        _ => {
            let region_id = memory_plan.decode_tensor_region.get(&tid).copied().unwrap_or(0);
            (format!("s->act_r[{}]", region_id), 0)
        }
    }
}

/// Emit `[enc setBuffer:...]` for decode path with split region buffers.
fn emit_buffer_bindings_decode(
    node: &Node,
    graph: &crate::ir::graph::CompGraph,
    memory_plan: &MemoryPlan,
    indent: &str,
) -> String {
    let mut s = String::new();
    let (buf, off) = resolve_buffer_decode(node.output, graph, memory_plan);
    s.push_str(&format!("{indent}[enc setBuffer:{buf} offset:{off} atIndex:0];\n"));
    for (i, &inp) in node.inputs.iter().enumerate() {
        let (buf, off) = resolve_buffer_decode(inp, graph, memory_plan);
        s.push_str(&format!("{indent}[enc setBuffer:{buf} offset:{off} atIndex:{}];\n", i + 1));
    }
    s
}

/// Emit MLX sdpa_vector dispatch for decode path (seq_len=1).
/// Replaces steel_attention_causal_f16 with MLX's multi-simdgroup parallel SDPA.
fn emit_sdpa_vector_decode(
    node: &Node,
    graph: &crate::ir::graph::CompGraph,
    memory_plan: &MemoryPlan,
    head_dim: usize,
    gqa_factor: usize,
    scale: f32,
    p: &crate::ir::graph::ModelParams,
) -> String {
    let mut s = String::new();
    let indent = "        ";

    // Resolve buffers: inputs = [Q, K_cache, V_cache], output = attn_out
    let (q_buf, q_off) = resolve_buffer_decode(node.inputs[0], graph, memory_plan);
    let (k_buf, k_off) = resolve_buffer_decode(node.inputs[1], graph, memory_plan);
    let (v_buf, v_off) = resolve_buffer_decode(node.inputs[2], graph, memory_plan);
    let (out_buf, out_off) = resolve_buffer_decode(node.output, graph, memory_plan);

    // sdpa_vector buffer bindings
    s.push_str(&format!("{indent}[enc setBuffer:{q_buf} offset:{q_off} atIndex:0];\n"));   // queries
    s.push_str(&format!("{indent}[enc setBuffer:{k_buf} offset:{k_off} atIndex:1];\n"));   // keys
    s.push_str(&format!("{indent}[enc setBuffer:{v_buf} offset:{v_off} atIndex:2];\n"));   // values
    s.push_str(&format!("{indent}[enc setBuffer:{out_buf} offset:{out_off} atIndex:3];\n")); // out

    // gqa_factor (buffer 4, setBytes int)
    s.push_str(&format!("{indent}{{ int _gqa = {gqa_factor}; [enc setBytes:&_gqa length:4 atIndex:4]; }}\n"));

    // N = total_seq_len (buffer 5, setBytes int)
    s.push_str(&format!("{indent}{{ int _n = (int)total_seq_len; [enc setBytes:&_n length:4 atIndex:5]; }}\n"));

    // KV cache strides (in elements, not bytes)
    // KV cache layout: [num_kv_heads, max_seq_len, head_dim]
    let max_seq = p.max_position_embeddings;
    let k_head_stride = max_seq * head_dim;  // stride between KV heads
    let k_seq_stride = head_dim;             // stride between seq positions

    s.push_str(&format!("{indent}{{ size_t _s = {k_head_stride}; [enc setBytes:&_s length:8 atIndex:6]; }}\n")); // k_head_stride
    s.push_str(&format!("{indent}{{ size_t _s = {k_seq_stride}; [enc setBytes:&_s length:8 atIndex:7]; }}\n"));  // k_seq_stride
    s.push_str(&format!("{indent}{{ size_t _s = {k_head_stride}; [enc setBytes:&_s length:8 atIndex:8]; }}\n")); // v_head_stride
    s.push_str(&format!("{indent}{{ size_t _s = {k_seq_stride}; [enc setBytes:&_s length:8 atIndex:9]; }}\n"));  // v_seq_stride

    // scale (buffer 10, setBytes float)
    let scale_bits = scale.to_bits();
    s.push_str(&format!("{indent}{{ uint32_t _sb = {scale_bits}u; float _sc; memcpy(&_sc, &_sb, 4); [enc setBytes:&_sc length:4 atIndex:10]; }}\n"));

    // Dispatch: grid = (num_q_heads, 1, 1), threads = (32, 32, 1) = 1024 threads
    // BN=32 simdgroups process KV positions in parallel
    let num_q_heads = p.num_attention_heads;
    s.push_str(&format!("{indent}DISPATCH_BARRIER();\n"));
    s.push_str(&format!("{indent}[enc setComputePipelineState:pso_sdpa_vector];\n"));
    s.push_str(&format!("{indent}[enc dispatchThreadgroups:MTLSizeMake({num_q_heads}, 1, 1) \
         threadsPerThreadgroup:MTLSizeMake(32, 32, 1)];\n"));

    s
}

/// Emit buffer bindings for decode path with quantized weights + split region buffers.
fn emit_buffer_bindings_quant_decode(
    node: &Node,
    graph: &crate::ir::graph::CompGraph,
    memory_plan: &MemoryPlan,
    quant_map: &QuantOffsetMap,
    indent: &str,
) -> String {
    let mut s = String::new();
    let (buf, off) = resolve_buffer_decode(node.output, graph, memory_plan);
    s.push_str(&format!("{indent}[enc setBuffer:{buf} offset:{off} atIndex:0];\n"));
    for (i, &inp) in node.inputs.iter().enumerate() {
        let tensor = graph.tensors.get(&inp);
        let is_weight = tensor.map_or(false, |t| matches!(t.storage, StorageClass::Weight(_)));
        if is_weight {
            if let Some(t) = tensor {
                if let StorageClass::Weight(w) = &t.storage {
                    let q_off = quant_map.get(&w.byte_offset).copied().unwrap_or(0);
                    s.push_str(&format!(
                        "{indent}[enc setBuffer:s->quant_weight_buf offset:{q_off} atIndex:{}];\n",
                        i + 1
                    ));
                }
            }
        } else {
            let (buf, off) = resolve_buffer_decode(inp, graph, memory_plan);
            s.push_str(&format!(
                "{indent}[enc setBuffer:{buf} offset:{off} atIndex:{}];\n",
                i + 1
            ));
        }
    }
    s
}

/// Emit `[enc setBytes:...]` for op-specific scalar params at [[buffer(4)]].
///
/// params[8] layout: [seq_len, act_stride, K, extra0, extra1, extra2, extra3, extra4]
///   seq_len and act_stride are runtime; remaining fields are static per model config.
///   act_stride = N_max for prefill, 1 for decode (compact buffer).
fn emit_op_params(
    node: &Node,
    graph: &crate::ir::graph::CompGraph,
    indent: &str,
) -> String {
    let p = &graph.metadata.params;

    let out_shape = graph.tensors.get(&node.output)
        .map(|t| t.shape.max_shape())
        .unwrap_or_default();
    // M = first static dim of output (e.g. h_out for GEMM, h for elementwise)
    let m_out = out_shape.first().copied().unwrap_or(p.hidden_size) as u32;

    let in0_shape = node.inputs.first()
        .and_then(|id| graph.tensors.get(id))
        .map(|t| t.shape.max_shape())
        .unwrap_or_default();

    // k = first dim of in0 (the static hidden/K dimension) for most ops.
    // Exceptions: Gather (embed table [vocab, h] → use last dim = h),
    //             KVCacheAppend (kv_cache [kv_heads, max_seq, head_dim] → use last dim = head_dim).
    let k: u32 = match &node.op {
        Op::Gather { .. } => in0_shape.last().copied().unwrap_or(p.hidden_size) as u32,
        Op::KVCacheAppend { .. } => in0_shape.last().copied().unwrap_or(p.head_dim) as u32,
        _ => in0_shape.first().copied().unwrap_or(p.hidden_size) as u32,
    };

    // For ScaledDotProductAttention and KVCacheAppend, emit total_seq_len
    // so the kernel can address the full KV cache correctly.
    match &node.op {
        Op::ScaledDotProductAttention { head_dim, gqa_factor, .. } => {
            // params[2] = max_position_embeddings (KV cache stride, always N_max)
            // params[6] = act_stride (N_max for prefill, 1 for compact decode)
            return format!(
                "{indent}{{ uint32_t _p[8] = {{seq_len, total_seq_len, {}, {}, {}, {}, act_stride, 0}};\n\
                 {indent}  [enc setBytes:_p length:32 atIndex:4]; }}\n",
                p.max_position_embeddings, head_dim, gqa_factor, p.num_kv_heads,
            );
        }
        Op::KVCacheAppend { layer, role } => {
            // params[2] = act_stride (activation read stride)
            // params[4] = max_position_embeddings (KV cache write stride)
            let role_u = if *role == KVRole::Key { 0u32 } else { 1u32 };
            return format!(
                "{indent}{{ uint32_t _p[8] = {{seq_len, total_seq_len, act_stride, {}, {}, {}, {}, 0}};\n\
                 {indent}  [enc setBytes:_p length:32 atIndex:4]; }}\n",
                p.head_dim, p.max_position_embeddings, layer, role_u,
            );
        }
        Op::RoPE { base, .. } => {
            // params: [seq_len, act_stride, rope_dim, head_dim, num_heads, base, total_seq_len, 0]
            let base_bits = (*base as f32).to_bits();
            return format!(
                "{indent}{{ uint32_t _p[8] = {{seq_len, act_stride, {}, {}, {}, {}, total_seq_len, 0}};\n\
                 {indent}  [enc setBytes:_p length:32 atIndex:4]; }}\n",
                k, p.head_dim, p.num_attention_heads, base_bits,
            );
        }
        Op::QKNorm { eps, num_heads, head_dim } => {
            // params: [seq_len, act_stride, head_dim, eps, num_heads, 0, 0, 0]
            let eps_bits = (*eps as f32).to_bits();
            return format!(
                "{indent}{{ uint32_t _p[8] = {{seq_len, act_stride, {head_dim}, {eps_bits}, {num_heads}, 0, 0, 0}};\n\
                 {indent}  [enc setBytes:_p length:32 atIndex:4]; }}\n",
            );
        }
        _ => {}
    }

    let extra: [u32; 5] = match &node.op {
        Op::RMSNorm { eps } => {
            let eps_bits = (*eps as f32).to_bits();
            [eps_bits, 0, 0, 0, 0]
        }
        // For GEMM/GEMV: extra[0] = M (output rows = h_out)
        Op::MatMul | Op::BatchMatMul { .. } | Op::LogitProjection | Op::MatVec
        | Op::QuantizedMatMul { .. } | Op::QuantizedMatVec { .. } => {
            [m_out, 0, 0, 0, 0]
        }
        Op::Gather { .. } => [p.vocab_size as u32, 0, 0, 0, 0],
        _ => [0, 0, 0, 0, 0],
    };

    // params[1] = act_stride (runtime: N_max for prefill, 1 for compact decode)
    format!(
        "{indent}{{ uint32_t _p[8] = {{seq_len, act_stride, {k}, {}, {}, {}, {}, {}}};\n\
         {indent}  [enc setBytes:_p length:32 atIndex:4]; }}\n",
        extra[0], extra[1], extra[2], extra[3], extra[4]
    )
}

// ---------------------------------------------------------------------------
// Dispatch helpers
// ---------------------------------------------------------------------------

fn emit_dispatch_indented(
    kernel_name: &str,
    dispatch: &DispatchConfig,
    seq_param: &str,
    indent: &str,
) -> String {
    let mut s = String::new();
    s.push_str(&format!("{indent}DISPATCH_BARRIER();\n"));
    let pso_var = pso_var_name(kernel_name);
    s.push_str(&format!(
        "{indent}[enc setComputePipelineState:{pso_var}];\n"
    ));

    if dispatch.shared_memory_bytes > 0 {
        s.push_str(&format!(
            "{indent}[enc setThreadgroupMemoryLength:{} atIndex:0];\n",
            dispatch.shared_memory_bytes
        ));
    }

    let gx = emit_dim(&dispatch.grid[0], seq_param);
    let gy = emit_dim(&dispatch.grid[1], seq_param);
    let gz = emit_dim(&dispatch.grid[2], seq_param);
    let tx = dispatch.threadgroup[0];
    let ty = dispatch.threadgroup[1];
    let tz = dispatch.threadgroup[2];

    s.push_str(&format!(
        "{indent}[enc dispatchThreadgroups:MTLSizeMake({gx}, {gy}, {gz}) \
         threadsPerThreadgroup:MTLSizeMake({tx}, {ty}, {tz})];\n"
    ));
    s
}

fn emit_dim(dim: &DispatchDim, seq_param: &str) -> String {
    match dim {
        DispatchDim::Static(v) => v.to_string(),
        DispatchDim::CeilDiv { param, divisor } => {
            let p = emit_param_name(*param, seq_param);
            format!("(({p} + {divisor} - 1) / {divisor})")
        }
        DispatchDim::Expr(expr) => emit_expr(expr, seq_param),
    }
}

fn emit_expr(expr: &ParamExpr, seq_param: &str) -> String {
    match expr {
        ParamExpr::Param(p) => emit_param_name(*p, seq_param),
        ParamExpr::Const(v) => v.to_string(),
        ParamExpr::CeilDiv(a, b) => {
            format!("(({} + {} - 1) / {})", emit_expr(a, seq_param), emit_expr(b, seq_param), emit_expr(b, seq_param))
        }
        ParamExpr::Mul(a, b) => format!("({} * {})", emit_expr(a, seq_param), emit_expr(b, seq_param)),
        ParamExpr::Add(a, b) => format!("({} + {})", emit_expr(a, seq_param), emit_expr(b, seq_param)),
        ParamExpr::Min(a, b) => format!("MIN({}, {})", emit_expr(a, seq_param), emit_expr(b, seq_param)),
    }
}

fn emit_param_name(param: ParamName, seq_param: &str) -> String {
    match param {
        ParamName::SeqLen => seq_param.to_string(),
        ParamName::TotalSeqLen => "total_seq_len".to_string(),
        ParamName::BatchSize => "1".to_string(),
    }
}

// ---------------------------------------------------------------------------
// Logit offset
// ---------------------------------------------------------------------------

fn find_logit_offset(result: &CompilationResult) -> usize {
    let graph = &result.graph;
    let memory_plan = &result.memory_plan;

    // Primary: use the declared graph output
    if let Some(&logit_id) = graph.outputs.first() {
        for alloc in &memory_plan.activation_buffers {
            if alloc.aliases.contains(&logit_id) {
                return alloc.offset;
            }
        }
    }

    // Fallback: last node's output tensor
    if let Some(last_node) = graph.nodes.last() {
        for alloc in &memory_plan.activation_buffers {
            if alloc.aliases.contains(&last_node.output) {
                return alloc.offset;
            }
        }
    }

    log::warn!("Could not resolve logit offset from memory plan — defaulting to 0");
    0
}

// ---------------------------------------------------------------------------
// Weight layout
// ---------------------------------------------------------------------------

fn build_weight_layout(result: &CompilationResult) -> WeightFileLayout {
    let graph = &result.graph;
    let mut tensors = Vec::new();
    let mut total_bytes = 0usize;

    for tensor in graph.tensors.values() {
        if let StorageClass::Weight(ref w) = tensor.storage {
            if w.byte_size > 0 {
                tensors.push(WeightTensorLayout {
                    name: w.name.clone(),
                    offset: w.byte_offset,
                    size_bytes: w.byte_size,
                    dtype: w.file_dtype,
                    shape: w.file_shape.clone(),
                });
                total_bytes = total_bytes.max(w.byte_offset + w.byte_size);
            }
        }
    }

    tensors.sort_by_key(|t| t.offset);

    WeightFileLayout {
        strategy: WeightStrategy::MmapSafetensors,
        tensors,
        total_bytes,
    }
}
