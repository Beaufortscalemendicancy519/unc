/// Per-tensor mapping: f16 byte offset → quantized byte offset (block-aligned).
type QuantOffsetMap = HashMap<usize, usize>;

/// Collect weight tensors and compute aligned quantized buffer offsets.
fn build_quant_offset_map(result: &CompilationResult, quant: &str) -> (QuantOffsetMap, usize) {
    let graph = &result.graph;
    let (block_bytes, group_size): (usize, usize) = match quant {
        "q8_0" => (34, 32),
        "q4_0" => (18, 32),
        "q4_mlx" => (68, 128),
        _ => return (HashMap::new(), 0),
    };

    // Collect all weight tensors sorted by f16 offset for deterministic layout
    let mut weights: Vec<(usize, usize)> = Vec::new(); // (f16_offset, f16_size)
    for tensor in graph.tensors.values() {
        if let StorageClass::Weight(ref w) = tensor.storage {
            if w.byte_size > 0 {
                weights.push((w.byte_offset, w.byte_size));
            }
        }
    }
    weights.sort_by_key(|&(off, _)| off);
    weights.dedup();

    let mut map = HashMap::new();
    let mut running_q_offset: usize = 0;
    for (f16_offset, f16_size) in &weights {
        map.insert(*f16_offset, running_q_offset);
        let n_elements = f16_size / 2;
        let n_blocks = (n_elements + group_size - 1) / group_size;
        running_q_offset += n_blocks * block_bytes;
    }

    (map, running_q_offset)
}

/// Convert kernel name to C variable name for static PSO caching
fn pso_var_name(kernel_name: &str) -> String {
    format!("pso_{}", kernel_name.replace('-', "_"))
}

pub fn generate_orchestrator(result: &CompilationResult, unfused: bool) -> String {
    let graph = &result.graph;
    let memory_plan = &result.memory_plan;
    let p = &graph.metadata.params;

    let logit_offset = find_logit_offset(result);

    let mut src = String::new();

    src.push_str(&format!(
        r#"// Auto-generated by unc — Universal Neural Compiler
// Model: {}
// Architecture: {:?}
// Layers: {}, Heads: {}, Hidden: {}

#import <Metal/Metal.h>
#import <Foundation/Foundation.h>
#include <stdint.h>
#include <stdlib.h>
#include <string.h>

// ---------------------------------------------------------------------------
// Constants
// ---------------------------------------------------------------------------
#define UNC_NUM_LAYERS       {}
#define UNC_HIDDEN_SIZE      {}
#define UNC_NUM_HEADS        {}
#define UNC_NUM_KV_HEADS     {}
#define UNC_HEAD_DIM         {}
#define UNC_FFN_SIZE         {}
#define UNC_VOCAB_SIZE       {}
#define UNC_MAX_SEQ_LEN      {}
#define UNC_ACTIVATION_BYTES {}
#define UNC_DECODE_ACT_BYTES (UNC_ACTIVATION_BYTES / UNC_MAX_SEQ_LEN)
#define UNC_KV_CACHE_BYTES   {}
#define UNC_LOGIT_OFFSET     {}
#define UNC_NUM_DECODE_REGIONS {}

"#,
        graph.metadata.model_id,
        graph.metadata.architecture,
        p.num_hidden_layers, p.num_attention_heads, p.hidden_size,
        p.num_hidden_layers,
        p.hidden_size,
        p.num_attention_heads,
        p.num_kv_heads,
        p.head_dim,
        p.intermediate_size,
        p.vocab_size,
        p.max_position_embeddings,
        memory_plan.total_activation_bytes,
        memory_plan.total_kv_cache_bytes,
        logit_offset,
        memory_plan.decode_regions.len(),
    ));

    src.push_str(
        r#"// ---------------------------------------------------------------------------
// Pipeline state
// ---------------------------------------------------------------------------
typedef struct {
    id<MTLDevice>              device;
    id<MTLCommandQueue>        queue;
    id<MTLLibrary>             library;
    // One pipeline state per unique kernel
    NSMutableDictionary*       pipelines;   // NSString* name -> id<MTLComputePipelineState>
    // Memory regions
    id<MTLBuffer>              weight_buf;  // mmap'd weights (no-copy)
    id<MTLBuffer>              act_buf;     // activation scratch space (prefill: full [h,N_max] layout)
    id<MTLBuffer>              decode_act_buf; // compact decode buffer (stride=1, 1/N_max the size)
    id<MTLBuffer>              kv_buf;      // KV cache (pre-allocated)
    id<MTLBuffer>              logit_buf;   // f32 logit output [vocab_size]
    id<MTLBuffer>              quant_weight_buf; // quantized weights (Q8_0/Q4_0), nil if f16
    id<MTLBuffer>              psq_buf;          // partial sum-of-squares for PSQ norm pipeline (float[max_tgs])
    id<MTLBuffer>              mega_layer_buf;   // megakernel per-layer params
    id<MTLBuffer>              mega_sync_buf;    // megakernel cross-TG sync buffer
    id<MTLBuffer>              fused_sync_buf;   // fused RoPE+KV+SDPA cross-TG sync (uint32[num_layers])
    id<MTLBuffer>              fused_attn_buf;   // dedicated SDPA output buffer (avoids aliasing with v_in)
    id<MTLBuffer>              rope_table;       // precomputed cos/sin for RoPE [max_seq * half_dim * 2] half
    id<MTLBuffer>              act_r[UNC_NUM_DECODE_REGIONS]; // split decode activation regions
} UNCState;

static UNCState* unc_state = NULL;

// Compact decode buffer: offset mapping for activation tensors.
// All activation tensors are [h, N_max] layout, so compact_offset = full_offset / N_max.
static inline NSUInteger act_off(NSUInteger full_offset, uint32_t seq_len) {
    return (seq_len > 1) ? full_offset : (full_offset / UNC_MAX_SEQ_LEN);
}

"#,
    );

    // Collect unique kernel names for PSO pre-warming
    let mut kernel_names: Vec<String> = Vec::new();
    for node in graph.nodes.iter() {
        if let Some(ka) = &node.kernel {
            if !kernel_names.contains(&ka.kernel_name) {
                kernel_names.push(ka.kernel_name.clone());
            }
            if let ExecutionPath::Dual { prefill_kernel_name, decode_kernel_name, .. } = &ka.path {
                if !kernel_names.contains(prefill_kernel_name) {
                    kernel_names.push(prefill_kernel_name.clone());
                }
                if !kernel_names.contains(decode_kernel_name) {
                    kernel_names.push(decode_kernel_name.clone());
                }
            }
        }
    }
    kernel_names.push("logit_gather_f32".to_string());
    kernel_names.push("fused_qkv_gemv_f16".to_string());
    kernel_names.push("fused_gate_up_gemv_f16".to_string());

    let quant = graph.metadata.quant.as_str();
    let q4_suffix = if quant == "q4_mlx" { "q4_mlx" } else { "q4_0" };
    let quant_active = matches!(quant, "q8_0" | "q4_0" | "q4_mlx");
    if quant_active {
        // Fast vectorized GEMV kernels (separated scale/data layout)
        let q_gemv = format!("qmv_fast_{}", quant);
        if !kernel_names.contains(&q_gemv) {
            kernel_names.push(q_gemv);
        }
        let q_convert = format!("quantize_f16_to_{}_fast", quant);
        if !kernel_names.contains(&q_convert) {
            kernel_names.push(q_convert);
        }
        // Fused quantized GEMV kernels
        if quant == "q4_0" || quant == "q4_mlx" {
            kernel_names.push(format!("fused_qkv_qmv_fast_{}", quant));
            kernel_names.push(format!("fused_gate_up_qmv_fast_{}", quant));
            kernel_names.push(format!("fused_gate_up_swiglu_qmv_fast_{}", quant));
        }
    }
    // SwiGLU kernel
    kernel_names.push("swiglu_f16".to_string());
    // Fused kernels
    kernel_names.push("add_rms_norm_f16".to_string());
    kernel_names.push("rope_qk_f16".to_string());
    kernel_names.push("kv_cache_append_fused_f16".to_string());
    kernel_names.push("rope_qk_kv_append_f16".to_string());
    kernel_names.push("mega_decode_q4_0".to_string());
    // Superfused and PSQ pipeline kernels (quant-dependent)
    kernel_names.push(format!("fused_add_rmsnorm_qkv_qmv_fast_{}", q4_suffix));
    kernel_names.push(format!("fused_add_rmsnorm_gate_up_swiglu_qmv_fast_{}", q4_suffix));
    kernel_names.push(format!("qmv_add_psq_{}", q4_suffix));
    kernel_names.push(format!("fused_qkv_norm_qmv_fast_{}", q4_suffix));
    kernel_names.push(format!("fused_gate_up_swiglu_norm_qmv_fast_{}", q4_suffix));
    // Fused RoPE+KV+SDPA kernel (decode only)
    kernel_names.push("fused_rope_kv_sdpa_f16".to_string());
    // Fused SDPA+RoPE+KV kernel (decode only, eliminates RoPE dispatch)
    kernel_names.push("sdpa_rope_kv_decode_f16".to_string());
    // HEAD_DIM=128 variant for Llama-2/3, Mistral, Qwen, Gemma
    kernel_names.push("sdpa_rope_kv_decode_f16_128".to_string());
    // QKNorm (Qwen3-style per-head normalization)
    if p.qk_norm && !kernel_names.contains(&"qk_norm_f16".to_string()) {
        kernel_names.push("qk_norm_f16".to_string());
    }
    // MLX kernel variants
    kernel_names.push("unc_mlx_qmv_fast_q4_0".to_string());
    // Standalone Q4_MLX QMV
    if quant == "q4_mlx" {
        kernel_names.push("qmv_fast_q4_mlx".to_string());
    }
    // sdpa_vector PSOs are created separately with function constants (not via get_pipeline)

    // Emit static PSO variables (eliminates NSDictionary lookup per dispatch)
    for name in &kernel_names {
        let var = pso_var_name(name);
        src.push_str(&format!("static id<MTLComputePipelineState> {var};\n"));
    }
    // sdpa_vector PSO (created with function constants, not via get_pipeline)
    src.push_str("static id<MTLComputePipelineState> pso_sdpa_vector;\n");

    // Keep get_pipeline for init-time use only
    src.push_str(r#"
static id<MTLComputePipelineState> get_pipeline(const char* name) {
    NSString* key = [NSString stringWithUTF8String:name];
    id<MTLComputePipelineState> pso = unc_state->pipelines[key];
    if (pso == nil) {
        NSError* err = nil;
        id<MTLFunction> fn = [unc_state->library newFunctionWithName:key];
        if (fn == nil) { NSLog(@"Kernel function not found: %s", name); abort(); }
        pso = [unc_state->device newComputePipelineStateWithFunction:fn error:&err];
        if (err) { NSLog(@"PSO error for %s: %@", name, err); abort(); }
        unc_state->pipelines[key] = pso;
    }
    return pso;
}
"#);

    let quant_map = if quant_active {
        let (map, total) = build_quant_offset_map(result, quant);
        src.push_str(&generate_init_fn(&kernel_names, quant, result, &map, total));
        src.push_str(&generate_forward_fn(result, quant, &map, unfused));
        map
    } else {
        src.push_str(&generate_init_fn(&kernel_names, quant, result, &HashMap::new(), 0));
        src.push_str(&generate_forward_fn(result, quant, &HashMap::new(), unfused));
        HashMap::new()
    };
    let _ = quant_map;

    src
}

fn generate_init_fn(kernel_names: &[String], quant: &str, result: &CompilationResult, quant_map: &QuantOffsetMap, quant_total_bytes: usize) -> String {
    let mut s = String::new();
    s.push_str(r#"// ---------------------------------------------------------------------------
// Initialization
// ---------------------------------------------------------------------------
void unc_init(const char* metallib_path, void* weight_data, size_t weight_size, uint8_t weights_already_converted) {
    unc_state = (UNCState*)calloc(1, sizeof(UNCState));

    unc_state->device = MTLCreateSystemDefaultDevice();
    assert(unc_state->device != nil);

    unc_state->queue = [unc_state->device newCommandQueue];

    // Load metallib
    NSError* err = nil;
    NSURL* url = [NSURL fileURLWithPath:[NSString stringWithUTF8String:metallib_path]];
    unc_state->library = [unc_state->device newLibraryWithURL:url error:&err];
    if (err) {
        NSLog(@"Failed to load metallib from %s: %@", metallib_path, err);
        abort();
    }

    unc_state->pipelines = [NSMutableDictionary new];

    // Wrap weight data in a Metal buffer.
    // newBufferWithBytesNoCopy requires page-aligned length; use newBufferWithBytes for safety.
    unc_state->weight_buf = [unc_state->device
        newBufferWithBytes:weight_data
        length:weight_size
        options:MTLResourceStorageModeShared | MTLResourceHazardTrackingModeUntracked];

    // Convert BF16 weights to IEEE F16 in-place on GPU (skip if already converted).
    // BF16 = sign(1) exp(8) mantissa(7);  F16 = sign(1) exp(5) mantissa(10)
    if (!weights_already_converted) {
        NSError* cvt_err = nil;
        id<MTLFunction> cvt_fn = [unc_state->library newFunctionWithName:@"bf16_to_f16"];
        if (cvt_fn == nil) {
            NSLog(@"bf16_to_f16 kernel not found, falling back to CPU conversion");
            uint16_t* p = (uint16_t*)[unc_state->weight_buf contents];
            size_t n = weight_size / 2;
            for (size_t i = 0; i < n; i++) {
                uint16_t b = p[i];
                uint16_t sign = (b >> 15) & 1;
                int exp8 = (b >> 7) & 0xFF;
                uint16_t mant7 = b & 0x7F;
                uint16_t f16;
                if (exp8 == 0) f16 = (sign << 15);
                else if (exp8 == 0xFF) f16 = (sign << 15) | (0x1F << 10) | ((uint16_t)mant7 << 3);
                else {
                    int u = exp8 - 127;
                    if (u > 15) f16 = (sign << 15) | (0x1F << 10);
                    else if (u < -14) f16 = (sign << 15);
                    else { uint16_t e5 = (uint16_t)(u + 15); f16 = (sign << 15) | (e5 << 10) | ((uint16_t)mant7 << 3); }
                }
                p[i] = f16;
            }
            NSLog(@"BF16->F16 CPU conversion: %zu elements", n);
        } else {
            id<MTLComputePipelineState> cvt_pso = [unc_state->device newComputePipelineStateWithFunction:cvt_fn error:&cvt_err];
            uint32_t n_elements = (uint32_t)(weight_size / 2);
            id<MTLCommandBuffer> cvt_cmd = [unc_state->queue commandBuffer];
            id<MTLComputeCommandEncoder> cvt_enc = [cvt_cmd computeCommandEncoder];
            [cvt_enc setComputePipelineState:cvt_pso];
            [cvt_enc setBuffer:unc_state->weight_buf offset:0 atIndex:0];
            [cvt_enc setBytes:&n_elements length:4 atIndex:1];
            [cvt_enc dispatchThreadgroups:MTLSizeMake((n_elements + 255) / 256, 1, 1)
                    threadsPerThreadgroup:MTLSizeMake(256, 1, 1)];
            [cvt_enc endEncoding];
            [cvt_cmd commit];
            [cvt_cmd waitUntilCompleted];
            NSLog(@"BF16->F16 GPU conversion: %u elements", n_elements);
        }
    }


    // Quantize weights on GPU (if quant mode active)
    unc_state->quant_weight_buf = nil;
"#);

    let quant_active = matches!(quant, "q8_0" | "q4_0" | "q4_mlx");
    if quant_active {
        // Use fast quantization kernels that write separated scale/data layout
        let kernel_name = match quant {
            "q8_0" => "quantize_f16_to_q8_0_fast",
            "q4_0" => "quantize_f16_to_q4_0_fast",
            "q4_mlx" => "quantize_f16_to_q4_mlx_fast",
            _ => unreachable!(),
        };
        let group_size: usize = if quant == "q4_mlx" { 128 } else { 32 };

        // Collect weight tensors sorted by f16 offset (same order as quant_map)
        // Also capture K dimension (last dim of shape) for the fast quantization kernel
        let graph = &result.graph;
        let mut weights: Vec<(usize, usize, usize)> = Vec::new(); // (f16_offset, f16_size, k_dim)
        for tensor in graph.tensors.values() {
            if let StorageClass::Weight(ref w) = tensor.storage {
                if w.byte_size > 0 {
                    let shape = tensor.shape.max_shape();
                    let k_dim = shape.last().copied().unwrap_or(1);
                    weights.push((w.byte_offset, w.byte_size, k_dim));
                }
            }
        }
        weights.sort_by_key(|&(off, _, _)| off);
        weights.dedup();

        s.push_str(&format!(r#"    {{
        // Per-tensor quantization: separated scale/data layout for vectorized GEMV
        size_t quant_buf_size = (size_t){quant_total_bytes};
        unc_state->quant_weight_buf = [unc_state->device
            newBufferWithLength:quant_buf_size
            options:MTLResourceStorageModeShared | MTLResourceHazardTrackingModeUntracked];
        id<MTLComputePipelineState> q_pso = get_pipeline("{kernel_name}");
        id<MTLCommandBuffer> q_cmd = [unc_state->queue commandBuffer];
        id<MTLComputeCommandEncoder> q_enc = [q_cmd computeCommandEncoder];
        [q_enc setComputePipelineState:q_pso];
"#));

        for (f16_offset, f16_size, k_dim) in &weights {
            let q_offset = quant_map.get(f16_offset).copied().unwrap_or(0);
            let n_elements = f16_size / 2;
            let n_blocks = (n_elements + group_size - 1) / group_size;
            let nb_per_row = k_dim / group_size;
            let m_val = if *k_dim > 0 { n_elements / k_dim } else { 1 };
            s.push_str(&format!(
                "        {{ uint32_t nb = {n_blocks}u; uint32_t nbr = {nb_per_row}u; uint32_t mv = {m_val}u;\n\
                 \x20         [q_enc setBuffer:unc_state->weight_buf offset:{f16_offset} atIndex:0];\n\
                 \x20         [q_enc setBuffer:unc_state->quant_weight_buf offset:{q_offset} atIndex:1];\n\
                 \x20         [q_enc setBytes:&nb length:4 atIndex:2];\n\
                 \x20         [q_enc setBytes:&nbr length:4 atIndex:3];\n\
                 \x20         [q_enc setBytes:&mv length:4 atIndex:4];\n\
                 \x20         [q_enc dispatchThreadgroups:MTLSizeMake(({n_blocks}u + 255u) / 256u, 1, 1)\n\
                 \x20                 threadsPerThreadgroup:MTLSizeMake(256, 1, 1)]; }}\n"
            ));
        }

        s.push_str(&format!(r#"        [q_enc endEncoding];
        [q_cmd commit];
        [q_cmd waitUntilCompleted];
        NSLog(@"Quantized {} weight tensors to {quant} (fast layout): %zu bytes (%.1fx compression)",
              quant_buf_size, (double)weight_size / (double)quant_buf_size);
    }}
"#, weights.len()));
    }

    s.push_str(r#"
    // Allocate activation scratch buffers
    // Full buffer for prefill (strided [h, N_max] layout)
    unc_state->act_buf = [unc_state->device
        newBufferWithLength:UNC_ACTIVATION_BYTES
        options:MTLResourceStorageModeShared | MTLResourceHazardTrackingModeUntracked];
    // Compact buffer for decode (contiguous stride=1 layout, N_max times smaller)
    unc_state->decode_act_buf = [unc_state->device
        newBufferWithLength:UNC_DECODE_ACT_BYTES
        options:MTLResourceStorageModeShared | MTLResourceHazardTrackingModeUntracked];

    // Allocate KV cache
    unc_state->kv_buf = [unc_state->device
        newBufferWithLength:UNC_KV_CACHE_BYTES
        options:MTLResourceStorageModeShared | MTLResourceHazardTrackingModeUntracked];

    // Allocate logit output buffer (f32)
    unc_state->logit_buf = [unc_state->device
        newBufferWithLength:(UNC_VOCAB_SIZE * sizeof(float))
        options:MTLResourceStorageModeShared | MTLResourceHazardTrackingModeUntracked];

    // PSQ buffer for partial sum-of-squares norm pipeline (max 512 TGs, float each)
    unc_state->psq_buf = [unc_state->device
        newBufferWithLength:(512 * sizeof(float))
        options:MTLResourceStorageModeShared | MTLResourceHazardTrackingModeUntracked];

    // Fused RoPE+KV+SDPA cross-TG sync buffer (uint32 per layer, zeroed)
    unc_state->fused_sync_buf = [unc_state->device
        newBufferWithLength:(64 * sizeof(uint32_t))
        options:MTLResourceStorageModeShared | MTLResourceHazardTrackingModeUntracked];
    memset([unc_state->fused_sync_buf contents], 0, 64 * sizeof(uint32_t));

    // Dedicated SDPA output buffer — avoids aliasing with v_in in fused RoPE+KV+SDPA
    // Size: num_heads * head_dim * sizeof(half) = attn_out dimension
    unc_state->fused_attn_buf = [unc_state->device
        newBufferWithLength:(UNC_NUM_HEADS * UNC_HEAD_DIM * sizeof(__fp16))
        options:MTLResourceStorageModeShared | MTLResourceHazardTrackingModeUntracked];

    // Precomputed RoPE cos/sin table: eliminates pow/sin/cos per token
    // Layout: rope_table[pos * half_dim * 2 + pair * 2 + 0] = cos(theta)
    //         rope_table[pos * half_dim * 2 + pair * 2 + 1] = sin(theta)
    {
        uint32_t half_dim = UNC_HEAD_DIM / 2;
        uint32_t table_elems = UNC_MAX_SEQ_LEN * half_dim * 2;
        unc_state->rope_table = [unc_state->device
            newBufferWithLength:(table_elems * sizeof(__fp16))
            options:MTLResourceStorageModeShared | MTLResourceHazardTrackingModeUntracked];
        __fp16* tbl = (__fp16*)[unc_state->rope_table contents];
"#);

    // Emit the rope_base as a float literal
    let p = &result.graph.metadata.params;
    let rope_base = p.rope_theta as f32;
    let head_dim = p.head_dim;

    s.push_str(&format!(
        "        float rope_base = {rope_base:.1}f;\n\
         \x20       for (uint32_t pos = 0; pos < UNC_MAX_SEQ_LEN; pos++) {{\n\
         \x20           for (uint32_t pair = 0; pair < half_dim; pair++) {{\n\
         \x20               float theta = (float)pos / powf(rope_base, 2.0f * (float)pair / (float){head_dim});\n\
         \x20               uint32_t idx = (pos * half_dim + pair) * 2;\n\
         \x20               tbl[idx]     = (__fp16)cosf(theta);\n\
         \x20               tbl[idx + 1] = (__fp16)sinf(theta);\n\
         \x20           }}\n\
         \x20       }}\n\
         \x20       NSLog(@\"RoPE table: %u entries, %.1f KB\", table_elems, table_elems * 2.0f / 1024.0f);\n\
         \x20   }}\n\n"
    ));

    s.push_str(r#"    // Allocate split decode activation regions (for concurrent dispatch)
"#);

    for region in &result.memory_plan.decode_regions {
        s.push_str(&format!(
            "    unc_state->act_r[{}] = [unc_state->device newBufferWithLength:{} options:MTLResourceStorageModeShared | MTLResourceHazardTrackingModeUntracked];\n",
            region.region_id, region.size_bytes
        ));
    }

    s.push_str(r#"
    // Pre-warm all pipeline states and cache as static variables
"#);

    for name in kernel_names {
        let var = pso_var_name(name);
        s.push_str(&format!("    {var} = get_pipeline(\"{name}\");\n"));
    }

    s.push_str("    NSLog(@\"Pre-warmed all pipeline states\");\n\n");

    // Create sdpa_vector PSO with function constants
    let head_dim = result.graph.metadata.params.head_dim;
    let sdpa_kernel_name = format!("sdpa_vector_float16_t_{head_dim}_{head_dim}");
    s.push_str(&format!(r#"    {{
        MTLFunctionConstantValues* fc = [[MTLFunctionConstantValues alloc] init];
        BOOL no = NO, yes = YES;
        int blocks_val = 1;
        [fc setConstantValue:&no  type:MTLDataTypeBool atIndex:20]; // has_mask
        [fc setConstantValue:&no  type:MTLDataTypeBool atIndex:21]; // query_transposed
        [fc setConstantValue:&yes type:MTLDataTypeBool atIndex:22]; // do_causal
        [fc setConstantValue:&no  type:MTLDataTypeBool atIndex:23]; // bool_mask
        [fc setConstantValue:&no  type:MTLDataTypeBool atIndex:24]; // float_mask
        [fc setConstantValue:&no  type:MTLDataTypeBool atIndex:25]; // has_sinks
        [fc setConstantValue:&blocks_val type:MTLDataTypeInt atIndex:26]; // blocks
        NSError* sdpa_err = nil;
        id<MTLFunction> sdpa_fn = [unc_state->library newFunctionWithName:@"{sdpa_kernel_name}" constantValues:fc error:&sdpa_err];
        if (sdpa_fn == nil) {{ NSLog(@"sdpa_vector function not found: %@", sdpa_err); abort(); }}
        pso_sdpa_vector = [unc_state->device newComputePipelineStateWithFunction:sdpa_fn error:&sdpa_err];
        if (sdpa_err) {{ NSLog(@"sdpa_vector PSO error: %@", sdpa_err); abort(); }}
        NSLog(@"Created sdpa_vector PSO (head_dim={head_dim})");
    }}
"#));

    // Megakernel init — DISABLED (see decode path comment)


    s.push_str("}\n\n");

    s
}

fn generate_forward_fn(result: &CompilationResult, quant: &str, quant_map: &QuantOffsetMap, unfused: bool) -> String {
    let graph = &result.graph;
    let memory_plan = &result.memory_plan;
    let q4_suffix = if quant == "q4_mlx" { "q4_mlx" } else { "q4_0" };

    // Build activation tensor → byte offset in act_buf
    let act_index = build_activation_index(memory_plan);

    let mut src = String::new();

    // Find token_ids tensor offset in activation buffer
    let tok_input_id = graph.inputs.first().unwrap();
    let tok_offset = act_index.get(tok_input_id).copied().unwrap_or(0);
    log::debug!("Token IDs tensor {:?} → byte offset {}", tok_input_id, tok_offset);

    // Insert decode_barrier() function (filled after barrier analysis below)
    // We use a placeholder index and patch it after barrier analysis computes forward_preamble
    let forward_preamble_insert_pos = src.len();

    src.push_str(&format!(
        r#"// ---------------------------------------------------------------------------
// Forward pass
// ---------------------------------------------------------------------------
void unc_forward(
    const uint32_t* token_ids,
    uint32_t        seq_len,
    uint32_t        total_seq_len,
    float*          out_logits       // caller-allocated [seq_len * UNC_VOCAB_SIZE]
) {{
    UNCState* s = unc_state;

    static uint64_t _profile_count = 0;
    static double _encode_total = 0, _gpu_total = 0;
    struct timespec _ts0; clock_gettime(CLOCK_MONOTONIC_RAW, &_ts0);

"#,
    ));

    // Megakernel decode path — DISABLED: single-TG can't saturate memory bandwidth.
    // 1 TG = 1/14 compute units = ~14 GB/s (vs 200 GB/s needed for GEMVs).
    let tok_region = memory_plan.decode_tensor_region.get(graph.inputs.first().unwrap()).copied().unwrap_or(0);

    // NOTE: decode_barrier() function and forward preamble are emitted AFTER barrier analysis
    // (see below, after barrier_plan is computed)
    let mut forward_preamble = String::new(); // filled after barrier analysis

    // ---------------------------------------------------------------------------
    // Detect fusion groups for emitter-level GEMV fusion
    // ---------------------------------------------------------------------------
    // QKV groups: 3 consecutive Dual MatMul nodes sharing inputs[0]
    // Gate+Up pairs: 2 consecutive Dual MatMul nodes sharing inputs[0]
    use std::collections::HashSet;
    let mut skip_nodes: HashSet<usize> = HashSet::new();

    if unfused {
        log::info!("Unfused mode: all emit-time fusions disabled");
    }

    #[derive(Debug)]
    enum FusionGroup {
        QKV { q: usize, k: usize, v: usize },
        GateUp { gate: usize, up: usize },
    }
    let mut fusion_at: HashMap<usize, FusionGroup> = HashMap::new();

    // Find groups of Dual MatMul nodes sharing the same activation input
    // Only fuse for F16 and Q4_0 (fused Q8_0 kernels don't exist)
    let can_fuse = quant != "q8_0" && !unfused;
    {
        let mut i = 0;
        while i < graph.nodes.len() {
            let n = &graph.nodes[i];
            if !can_fuse || !matches!(n.op, Op::MatMul) || n.kernel.as_ref().map_or(true, |k| !matches!(k.path, ExecutionPath::Dual { .. })) {
                i += 1;
                continue;
            }
            let act_input = n.inputs[0];

            // Check for 3 consecutive MatMuls with same activation (QKV)
            if i + 2 < graph.nodes.len() {
                let n1 = &graph.nodes[i + 1];
                let n2 = &graph.nodes[i + 2];
                if matches!(n1.op, Op::MatMul) && matches!(n2.op, Op::MatMul)
                    && n1.inputs.get(0) == Some(&act_input)
                    && n2.inputs.get(0) == Some(&act_input)
                    && n1.kernel.as_ref().map_or(false, |k| matches!(k.path, ExecutionPath::Dual { .. }))
                    && n2.kernel.as_ref().map_or(false, |k| matches!(k.path, ExecutionPath::Dual { .. }))
                {
                    fusion_at.insert(i, FusionGroup::QKV { q: i, k: i + 1, v: i + 2 });
                    skip_nodes.insert(i + 1);
                    skip_nodes.insert(i + 2);
                    i += 3;
                    continue;
                }
            }
            // Check for 2 consecutive MatMuls with same activation (Gate+Up)
            if i + 1 < graph.nodes.len() {
                let n1 = &graph.nodes[i + 1];
                if matches!(n1.op, Op::MatMul)
                    && n1.inputs.get(0) == Some(&act_input)
                    && n1.kernel.as_ref().map_or(false, |k| matches!(k.path, ExecutionPath::Dual { .. }))
                {
                    fusion_at.insert(i, FusionGroup::GateUp { gate: i, up: i + 1 });
                    skip_nodes.insert(i + 1);
                    i += 2;
                    continue;
                }
            }
            i += 1;
        }
    }

    // Detect SwiGLU fusion: SiLU(x) followed by Mul(silu_out, y) → swiglu_f16(x, y)
    // The SiLU node gets replaced with swiglu_f16, the Mul node gets skipped
    #[allow(dead_code)]
    struct SwiGLUFusion {
        mul_idx: usize,       // index of Mul node to skip
        other_input: TensorId, // the non-SiLU input to Mul (the "up" tensor)
    }
    let mut swiglu_at: HashMap<usize, SwiGLUFusion> = HashMap::new(); // keyed by SiLU node index
    if !unfused {
        for (idx, node) in graph.nodes.iter().enumerate() {
            if !matches!(node.op, Op::SiLU) { continue; }
            let silu_out = node.output;
            // Find a Mul node that consumes this SiLU output
            for (midx, mnode) in graph.nodes.iter().enumerate() {
                if !matches!(mnode.op, Op::Mul) { continue; }
                if mnode.inputs.contains(&silu_out) {
                    let other = if mnode.inputs[0] == silu_out { mnode.inputs[1] } else { mnode.inputs[0] };
                    swiglu_at.insert(idx, SwiGLUFusion { mul_idx: midx, other_input: other });
                    skip_nodes.insert(midx);
                    break;
                }
            }
        }
    }

    // Detect Add + RMSNorm fusion: residual add followed by normalization
    struct AddRMSNormFusion { norm_idx: usize, norm_weight_input: TensorId }
    let mut add_rmsnorm_at: HashMap<usize, AddRMSNormFusion> = HashMap::new(); // keyed by Add node index
    if !unfused {
        for (idx, node) in graph.nodes.iter().enumerate() {
            if !matches!(node.op, Op::Add) { continue; }
            let add_out = node.output;
            // Find an RMSNorm node that consumes this Add's output
            for (nidx, nnode) in graph.nodes.iter().enumerate() {
                if !matches!(nnode.op, Op::RMSNorm { .. }) { continue; }
                if nnode.inputs.first() == Some(&add_out) {
                    // nnode.inputs[0] = x (the add output), nnode.inputs[1] = rmsnorm weight
                    if let Some(&weight_tid) = nnode.inputs.get(1) {
                        add_rmsnorm_at.insert(idx, AddRMSNormFusion { norm_idx: nidx, norm_weight_input: weight_tid });
                        skip_nodes.insert(nidx);
                    }
                    break;
                }
            }
        }
    }

    // Detect RoPE Q+K pairs: consecutive RoPE nodes on Q and K outputs
    struct RopeQKFusion { k_idx: usize }
    let mut rope_qk_at: HashMap<usize, RopeQKFusion> = HashMap::new(); // keyed by first RoPE node index
    if !unfused {
        let rope_indices: Vec<usize> = graph.nodes.iter().enumerate()
            .filter(|(_, n)| matches!(n.op, Op::RoPE { .. }))
            .map(|(i, _)| i)
            .collect();
        // Pair consecutive RoPE nodes (Q then K within same layer)
        for pair in rope_indices.chunks(2) {
            if pair.len() == 2 {
                rope_qk_at.insert(pair[0], RopeQKFusion { k_idx: pair[1] });
                skip_nodes.insert(pair[1]);
            }
        }
    }

    // Detect KVCacheAppend K+V pairs: same layer, different roles
    struct KVAppendFusion { v_idx: usize }
    let mut kv_append_at: HashMap<usize, KVAppendFusion> = HashMap::new();
    if !unfused {
        let append_indices: Vec<(usize, usize, KVRole)> = graph.nodes.iter().enumerate()
            .filter_map(|(i, n)| match &n.op {
                Op::KVCacheAppend { layer, role } => Some((i, *layer, role.clone())),
                _ => None,
            })
            .collect();
        // Group by layer and pair K+V
        let mut layer_appends: HashMap<usize, Vec<(usize, KVRole)>> = HashMap::new();
        for (idx, layer, role) in &append_indices {
            layer_appends.entry(*layer).or_default().push((*idx, role.clone()));
        }
        for (_layer, appends) in &layer_appends {
            if appends.len() == 2 {
                let (k_idx, v_idx) = if appends[0].1 == KVRole::Key {
                    (appends[0].0, appends[1].0)
                } else {
                    (appends[1].0, appends[0].0)
                };
                kv_append_at.insert(k_idx, KVAppendFusion { v_idx });
                skip_nodes.insert(v_idx);
            }
        }
    }

    // Detect super-fusion: RoPE QK + KV Append → single dispatch
    // When a RoPE QK pair is followed by a KV Append that reads the RoPE K output,
    // fuse all 4 ops (RoPE Q, RoPE K, KV Append K, KV Append V) into one kernel.
    struct RopeKVAppendFusion {
        kv_k_idx: usize,
        kv_v_idx: usize,
    }
    let mut rope_kv_at: HashMap<usize, RopeKVAppendFusion> = HashMap::new(); // keyed by RoPE Q node index
    {
        for (&rope_q_idx, rope) in &rope_qk_at {
            let rope_k_output = graph.nodes[rope.k_idx].output;
            for (&kv_k_idx, kv) in &kv_append_at {
                let kv_k_node = &graph.nodes[kv_k_idx];
                if kv_k_node.inputs.get(1) == Some(&rope_k_output) {
                    rope_kv_at.insert(rope_q_idx, RopeKVAppendFusion {
                        kv_k_idx,
                        kv_v_idx: kv.v_idx,
                    });
                    // Skip the KV Append K node (K and V nodes both handled by fused kernel)
                    skip_nodes.insert(kv_k_idx);
                    break;
                }
            }
        }
    }

    // Fused RoPE+KV+SDPA: merge RoPE+KV dispatch into SDPA kernel (sdpa_rope_kv_decode_f16).
    // The fused kernel applies RoPE via simd_shuffle, writes KV cache, and computes SDPA
    // in one dispatch with the same grid as sdpa_vector (num_q_heads × 32×32 threads).
    // This eliminates 22 dispatches (1 per layer).
    let mut rope_sdpa_fuse: HashMap<usize, usize> = HashMap::new();
    let mut sdpa_fused_by_rope: HashSet<usize> = HashSet::new();
    {
        for (&rope_q_idx, _rkv) in &rope_kv_at {
            let rope_q_output = graph.nodes[rope_q_idx].output;
            for (ni, n) in graph.nodes.iter().enumerate() {
                if matches!(n.op, Op::ScaledDotProductAttention { .. }) && n.inputs.get(0) == Some(&rope_q_output) {
                    rope_sdpa_fuse.insert(rope_q_idx, ni);
                    sdpa_fused_by_rope.insert(ni);
                    break;
                }
            }
        }
    }
    // Track SDPA outputs that live in fused_attn_buf (decode only)
    let fused_attn_tids: HashSet<TensorId> = HashSet::new();
    // With the new kernel, outputs go to regular region buffers, not fused_attn_buf
    if !rope_sdpa_fuse.is_empty() {
        log::info!("Fused RoPE+KV+SDPA: {} decode dispatches eliminated", rope_sdpa_fuse.len());
    }

    // Build reverse map: gate_output_tensor → SwiGLU output tensor for three-way fusion
    // Maps (gate_node_output_tid) → (swiglu_output_tid, silu_node_idx)
    let mut gate_to_swiglu: HashMap<TensorId, (TensorId, usize)> = HashMap::new();
    for (&silu_idx, swiglu) in &swiglu_at {
        let silu_node = &graph.nodes[silu_idx];
        let gate_out_tid = silu_node.inputs[0]; // SiLU's input = gate GEMV output
        let mul_node = &graph.nodes[swiglu.mul_idx];
        gate_to_swiglu.insert(gate_out_tid, (mul_node.output, silu_idx));
    }

    // Detect Add+RMSNorm → QKV / GateUp+SwiGLU super-fusions (decode only)
    // These eliminate the dispatch boundary between norm and GEMV by fusing them
    // into a single kernel that computes RMSNorm in shared memory.
    #[derive(Clone)]
    #[allow(dead_code)]
    struct NormGemvSuperFusion {
        add_node_idx: usize,
        norm_node_idx: usize,
        norm_weight_tid: TensorId,
        kind: NormGemvKind,
    }
    #[derive(Clone)]
    #[allow(dead_code)]
    enum NormGemvKind {
        QKV { q: usize, k: usize, v: usize },
        GateUpSwiGLU { gate: usize, up: usize, swiglu_out_tid: TensorId, silu_idx: usize },
    }
    // Map: first node of fusion group → super-fusion info
    let mut norm_gemv_super: HashMap<usize, NormGemvSuperFusion> = HashMap::new();
    // Map: add node index → super-fusion info (for skipping standalone Add+RMSNorm)
    let mut add_has_superfusion: HashMap<usize, usize> = HashMap::new(); // add_idx → first_gemv_idx

    let quant_active = quant == "q4_0" || quant == "q4_mlx" || quant == "q8_0";
    if false && quant_active && (quant == "q4_0" || quant == "q4_mlx") {
        for (&add_idx, fusion) in &add_rmsnorm_at {
            let norm_node = &graph.nodes[fusion.norm_idx];
            let norm_out = norm_node.output;
            let add_node_det = &graph.nodes[add_idx];
            let (_, x_off_det) = resolve_buffer(add_node_det.inputs[0], graph, memory_plan, &act_index, false);
            let (_, delta_off_det) = resolve_buffer(add_node_det.inputs[1], graph, memory_plan, &act_index, false);
            let inputs_det = [x_off_det, delta_off_det];

            // Check if norm output feeds into a QKV or GateUp fusion group
            for (&_fuse_idx, group) in &fusion_at {
                match group {
                    FusionGroup::QKV { q, k, v } => {
                        let q_node = &graph.nodes[*q];
                        if q_node.inputs[0] == norm_out {
                            // Check aliasing: GEMV outputs must not overlap with x or delta
                            let (_, oq_off) = resolve_buffer(q_node.output, graph, memory_plan, &act_index, false);
                            let (_, ok_off) = resolve_buffer(graph.nodes[*k].output, graph, memory_plan, &act_index, false);
                            let (_, ov_off) = resolve_buffer(graph.nodes[*v].output, graph, memory_plan, &act_index, false);
                            let safe = ![oq_off, ok_off, ov_off].iter().any(|o| inputs_det.contains(o));
                            if safe {
                                norm_gemv_super.insert(*q, NormGemvSuperFusion {
                                    add_node_idx: add_idx,
                                    norm_node_idx: fusion.norm_idx,
                                    norm_weight_tid: fusion.norm_weight_input,
                                    kind: NormGemvKind::QKV { q: *q, k: *k, v: *v },
                                });
                                add_has_superfusion.insert(add_idx, *q);
                            }
                        }
                    }
                    FusionGroup::GateUp { gate, up } => {
                        let gate_node = &graph.nodes[*gate];
                        if gate_node.inputs[0] == norm_out {
                            // Check for SwiGLU fusion
                            if let Some((swiglu_out_tid, silu_idx)) = gate_to_swiglu.get(&gate_node.output) {
                                // Check aliasing: output must not overlap with x or delta
                                let (_, out_off) = resolve_buffer(*swiglu_out_tid, graph, memory_plan, &act_index, false);
                                let safe = !inputs_det.contains(&out_off);
                                if safe {
                                    norm_gemv_super.insert(*gate, NormGemvSuperFusion {
                                        add_node_idx: add_idx,
                                        norm_node_idx: fusion.norm_idx,
                                        norm_weight_tid: fusion.norm_weight_input,
                                        kind: NormGemvKind::GateUpSwiGLU {
                                            gate: *gate, up: *up,
                                            swiglu_out_tid: *swiglu_out_tid,
                                            silu_idx: *silu_idx,
                                        },
                                    });
                                    add_has_superfusion.insert(add_idx, *gate);
                                }
                            }
                        }
                    }
                }
            }
        }
    }

    // -----------------------------------------------------------------------
    // PSQ (Partial Sum-of-Squares) pipeline detection (Q4_0 decode only)
    // Eliminates Add+RMSNorm dispatches by splitting the norm across two GEMVs:
    //   Writer (O-proj/Down-proj): GEMV + residual add + write partial sum-of-squares
    //   Reader (QKV/GateUp+SwiGLU): read partial sums → norm_scale → normalize on-the-fly
    // -----------------------------------------------------------------------
    struct PsqWriterInfo {
        residual_tid: TensorId,    // Add.inputs[0]
        add_output_tid: TensorId,  // Add.output (new residual)
    }
    #[derive(Clone)]
    struct PsqReaderInfo {
        norm_weight_tid: TensorId,
        eps: f32,
        num_psq_tgs: u32,         // number of partial sums = writer's grid_x
        activation_tid: TensorId,  // un-normalized residual sum (add_output)
    }
    let mut psq_writers: HashMap<usize, PsqWriterInfo> = HashMap::new();
    let mut psq_readers: HashMap<usize, PsqReaderInfo> = HashMap::new();
    let mut psq_skip_add: HashSet<usize> = HashSet::new();
    let psq_disabled = false;

    if (quant == "q4_0" || quant == "q4_mlx") && !psq_disabled && !unfused {
        let mut output_to_node: HashMap<TensorId, usize> = HashMap::new();
        for (idx, node) in graph.nodes.iter().enumerate() {
            output_to_node.insert(node.output, idx);
        }

        for (&add_idx, fusion) in &add_rmsnorm_at {
            let add_node = &graph.nodes[add_idx];
            let norm_node = &graph.nodes[fusion.norm_idx];
            let norm_out = norm_node.output;
            let delta_tid = add_node.inputs[1];
            let residual_tid = add_node.inputs[0];

            // Find the GEMV that produces the delta
            let writer_idx = match output_to_node.get(&delta_tid) {
                Some(&idx) => idx,
                None => continue,
            };
            let writer_node = &graph.nodes[writer_idx];

            // Must be a standalone Dual-path GEMV (not part of QKV/GateUp fusion)
            let is_dual_gemv = match &writer_node.kernel {
                Some(ka) => matches!(&ka.path, ExecutionPath::Dual { decode_kernel_name, .. } if decode_kernel_name == "gemv_f16"),
                None => false,
            };
            if !is_dual_gemv || fusion_at.contains_key(&writer_idx) { continue; }

            // Find which fusion group consumes the norm output
            for (&fuse_idx, group) in &fusion_at {
                let first_input = match group {
                    FusionGroup::QKV { q, .. } => graph.nodes[*q].inputs[0],
                    FusionGroup::GateUp { gate, .. } => graph.nodes[*gate].inputs[0],
                };
                if first_input != norm_out { continue; }

                let m_out = graph.tensors.get(&writer_node.output)
                    .map(|t| t.shape.max_shape()).unwrap_or_default()
                    .first().copied().unwrap_or(graph.metadata.params.hidden_size) as u32;
                let num_psq = (m_out + 15) / 16; // one partial sum per TG
                let eps = match &norm_node.op {
                    Op::RMSNorm { eps } => *eps as f32,
                    _ => 1e-5,
                };

                psq_writers.insert(writer_idx, PsqWriterInfo {
                    residual_tid,
                    add_output_tid: add_node.output,
                });
                psq_readers.insert(fuse_idx, PsqReaderInfo {
                    norm_weight_tid: fusion.norm_weight_input,
                    eps,
                    num_psq_tgs: num_psq,
                    activation_tid: add_node.output,
                });
                psq_skip_add.insert(add_idx);
                break;
            }
        }
    }
    let num_psq_pairs = psq_writers.len();
    if num_psq_pairs > 0 {
        log::info!("PSQ pipeline: {} writer/reader pairs, eliminating {} Add+RMSNorm decode dispatches", num_psq_pairs, psq_skip_add.len());
    }

    let num_qkv = fusion_at.values().filter(|g| matches!(g, FusionGroup::QKV { .. })).count();
    let num_gate_up = fusion_at.values().filter(|g| matches!(g, FusionGroup::GateUp { .. })).count();
    let num_swiglu = swiglu_at.len();
    let num_add_norm = add_rmsnorm_at.len();
    let num_rope_kv = rope_kv_at.len();
    let num_rope_qk_only = rope_qk_at.len() - num_rope_kv; // RoPE pairs not super-fused
    let num_kv_append_only = kv_append_at.len() - num_rope_kv; // KV appends not super-fused
    if num_qkv > 0 || num_gate_up > 0 || num_swiglu > 0 || num_add_norm > 0 || num_rope_kv > 0 || num_rope_qk_only > 0 || num_kv_append_only > 0 {
        src.push_str(&format!("    // Fusion: {} QKV, {} Gate+Up, {} SwiGLU, {} Add+RMSNorm, {} RoPE+KVAppend, {} RoPE Q+K, {} KVAppend K+V\n\n",
            num_qkv, num_gate_up, num_swiglu, num_add_norm, num_rope_kv, num_rope_qk_only, num_kv_append_only));
    }

    // ---------------------------------------------------------------------------
    // Barrier analysis: build dispatch units and compute selective barriers
    // ---------------------------------------------------------------------------
    let mut dispatch_units: Vec<DispatchUnit> = Vec::new();
    let mut node_to_dispatch: HashMap<usize, usize> = HashMap::new();
    {
        for (idx, node) in graph.nodes.iter().enumerate() {
            if skip_nodes.contains(&idx) { continue; }

            if let Some(group) = fusion_at.get(&idx) {
                // Fused dispatch: union of all inputs/outputs
                let nodes = match group {
                    FusionGroup::QKV { q, k, v } => vec![*q, *k, *v],
                    FusionGroup::GateUp { gate, up } => vec![*gate, *up],
                };
                let mut reads: Vec<TensorId> = Vec::new();
                let mut writes: Vec<TensorId> = Vec::new();
                for &ni in &nodes {
                    let n = &graph.nodes[ni];
                    reads.extend_from_slice(&n.inputs);
                    writes.push(n.output);
                }
                // For GateUp+SwiGLU three-way fusion, include SwiGLU output
                if let FusionGroup::GateUp { gate, .. } = group {
                    let gate_node = &graph.nodes[*gate];
                    if let Some((_swiglu_out_tid, silu_idx)) = gate_to_swiglu.get(&gate_node.output) {
                        let swiglu_fusion = swiglu_at.get(silu_idx).unwrap();
                        let mul_node = &graph.nodes[swiglu_fusion.mul_idx];
                        writes.push(mul_node.output);
                        reads.push(swiglu_fusion.other_input);
                    }
                }
                let di = dispatch_units.len();
                node_to_dispatch.insert(idx, di);
                dispatch_units.push(DispatchUnit { primary_node_idx: idx, reads, writes });
                continue;
            }

            // SwiGLU standalone (not part of GateUp fusion)
            if let Some(swiglu) = swiglu_at.get(&idx) {
                let mul_node = &graph.nodes[swiglu.mul_idx];
                let reads = vec![node.inputs[0], swiglu.other_input];
                let writes = vec![mul_node.output];
                let di = dispatch_units.len();
                node_to_dispatch.insert(idx, di);
                dispatch_units.push(DispatchUnit { primary_node_idx: idx, reads, writes });
                continue;
            }

            // Add+RMSNorm fusion
            if let Some(fusion) = add_rmsnorm_at.get(&idx) {
                let norm_node = &graph.nodes[fusion.norm_idx];
                let mut reads: Vec<TensorId> = node.inputs.clone();
                reads.push(fusion.norm_weight_input);
                let writes = vec![node.output, norm_node.output];
                let di = dispatch_units.len();
                node_to_dispatch.insert(idx, di);
                dispatch_units.push(DispatchUnit { primary_node_idx: idx, reads, writes });
                continue;
            }

            // RoPE+KVAppend super-fusion
            if let Some(rkv) = rope_kv_at.get(&idx) {
                let rope = rope_qk_at.get(&idx).unwrap();
                let k_rope_node = &graph.nodes[rope.k_idx];
                let kv_k_node = &graph.nodes[rkv.kv_k_idx];
                let kv_v_node = &graph.nodes[rkv.kv_v_idx];
                let mut reads: Vec<TensorId> = node.inputs.clone();
                reads.extend_from_slice(&k_rope_node.inputs);
                reads.push(kv_v_node.inputs[1]); // V input
                let writes = vec![node.output, kv_k_node.output, kv_v_node.output];
                let di = dispatch_units.len();
                node_to_dispatch.insert(idx, di);
                dispatch_units.push(DispatchUnit { primary_node_idx: idx, reads, writes });
                continue;
            }

            // RoPE QK standalone
            if let Some(fusion) = rope_qk_at.get(&idx) {
                if rope_kv_at.contains_key(&idx) { continue; }
                let k_node = &graph.nodes[fusion.k_idx];
                let mut reads: Vec<TensorId> = node.inputs.clone();
                reads.extend_from_slice(&k_node.inputs);
                let writes = vec![node.output, k_node.output];
                let di = dispatch_units.len();
                node_to_dispatch.insert(idx, di);
                dispatch_units.push(DispatchUnit { primary_node_idx: idx, reads, writes });
                continue;
            }

            // KVAppend K+V fusion
            if let Some(fusion) = kv_append_at.get(&idx) {
                let v_node = &graph.nodes[fusion.v_idx];
                let reads = vec![node.inputs[1], v_node.inputs[1]];
                let writes = vec![node.output, v_node.output];
                let di = dispatch_units.len();
                node_to_dispatch.insert(idx, di);
                dispatch_units.push(DispatchUnit { primary_node_idx: idx, reads, writes });
                continue;
            }

            // Regular node (unfused)
            if node.kernel.is_none() { continue; }
            let reads: Vec<TensorId> = node.inputs.clone();
            let writes = vec![node.output];
            let di = dispatch_units.len();
            node_to_dispatch.insert(idx, di);
            dispatch_units.push(DispatchUnit { primary_node_idx: idx, reads, writes });
        }
    }

    // Extend decode_tensor_region to include KV cache and PSQ buffer dependencies
    // KV cache tensors → virtual region 1000 (maps to s->kv_buf at emit time)
    // This allows barrier analysis to track KV cache read-after-write dependencies
    const KV_VIRTUAL_REGION: u32 = 1000;
    const PSQ_VIRTUAL_REGION: u32 = 1001;
    let mut extended_region_map = memory_plan.decode_tensor_region.clone();
    for tensor in graph.tensors.values() {
        if matches!(tensor.storage, StorageClass::KVCache { .. }) {
            extended_region_map.insert(tensor.id, KV_VIRTUAL_REGION);
        }
    }
    let barrier_plan = barrier_analysis::analyze_barriers(
        &dispatch_units,
        Some(&extended_region_map),
    );
    log::info!("Barrier plan: {} barriers for {} dispatches (was {} implicit)",
               barrier_plan.num_barriers, barrier_plan.num_dispatches, barrier_plan.num_dispatches);

    // Helper: check if a barrier should be emitted before this node's dispatch
    let _needs_barrier = |node_idx: usize| -> bool {
        node_to_dispatch.get(&node_idx)
            .map(|&di| barrier_plan.barrier_before.contains(&di))
            .unwrap_or(false)
    };

    // Helper: get per-resource barrier regions for a dispatch
    let _barrier_region_ids = |node_idx: usize| -> Vec<u32> {
        node_to_dispatch.get(&node_idx)
            .and_then(|&di| barrier_plan.barrier_regions.get(&di))
            .cloned()
            .unwrap_or_default()
    };

    // ---------------------------------------------------------------------------
    // Generate decode_barrier() function and forward preamble
    // (deferred from earlier because barrier_plan wasn't computed yet)
    // ---------------------------------------------------------------------------
    {
        let num_decode_regions = memory_plan.decode_regions.len();
        // _dbi removed — concurrent encoder uses explicit scope barriers via DISPATCH_BARRIER()
        forward_preamble.push_str("static inline void decode_barrier(id<MTLComputeCommandEncoder> enc, UNCState* s, int idx) {\n");
        forward_preamble.push_str("    switch(idx) {\n");
        for di in 0..barrier_plan.num_dispatches {
            if let Some(regions) = barrier_plan.barrier_regions.get(&di) {
                if regions.is_empty() { continue; }
                let mut resource_exprs: Vec<String> = Vec::new();
                for &rid in regions {
                    if rid == KV_VIRTUAL_REGION {
                        resource_exprs.push("s->kv_buf".to_string());
                    } else if rid == PSQ_VIRTUAL_REGION {
                        forward_preamble.push_str(""); // psq handled below
                        resource_exprs.push("s->psq_buf".to_string());
                    } else if (rid as usize) < num_decode_regions {
                        resource_exprs.push(format!("s->act_r[{}]", rid));
                    }
                }
                // Also include psq_buf for Q4_0 quant (PSQ pipeline writes partial sums)
                if !resource_exprs.iter().any(|e| e.contains("psq_buf")) && (quant == "q4_0" || quant == "q4_mlx") {
                    resource_exprs.push("s->psq_buf".to_string());
                }
                resource_exprs.dedup();
                let count = resource_exprs.len();
                let res_list = resource_exprs.join(", ");
                forward_preamble.push_str(&format!("    case {di}: [enc memoryBarrierWithResources:(id<MTLResource>[]){{{}}} count:{count}]; break;\n", res_list));
            }
        }
        forward_preamble.push_str("    default: break;\n");
        forward_preamble.push_str("    }\n");
        forward_preamble.push_str("}\n\n");
    }

    // Insert forward_preamble (decode_barrier function) BEFORE unc_forward
    src.insert_str(forward_preamble_insert_pos, &forward_preamble);

    let concurrent_encoder = if unfused {
        "[cmd computeCommandEncoderWithDispatchType:MTLDispatchTypeConcurrent]"
    } else {
        "[cmd computeCommandEncoder]"
    };
    src.push_str(&format!(
        r#"    id<MTLBuffer> cur_act = (seq_len > 1) ? s->act_buf : s->decode_act_buf;
    uint32_t act_stride = (seq_len > 1) ? UNC_MAX_SEQ_LEN : 1;

    id<MTLCommandBuffer> cmd = [s->queue commandBuffer];
    id<MTLComputeCommandEncoder> enc;
    if (seq_len > 1) {{
        enc = [cmd computeCommandEncoder];
    }} else {{
        enc = {concurrent_encoder};
    }}
    static int _dbi = 0;
    _dbi = 0;
    #define DISPATCH_BARRIER() do {{ if (seq_len == 1) decode_barrier(enc, s, _dbi); _dbi++; }} while(0)

    // Zero fused sync counters for this forward pass
    memset([s->fused_sync_buf contents], 0, 64 * sizeof(uint32_t));

    // Copy token IDs into the active activation buffer at correct offset
    if (seq_len > 1) {{
        memcpy((char*)[cur_act contents] + {tok_offset}, token_ids, seq_len * sizeof(uint32_t));
    }} else {{
        memcpy([s->act_r[{tok_region}] contents], token_ids, sizeof(uint32_t));
    }}

"#,
    ));

    // ---------------------------------------------------------------------------
    // Emit nodes
    // ---------------------------------------------------------------------------
    for (idx, node) in graph.nodes.iter().enumerate() {
        if skip_nodes.contains(&idx) {
            continue;
        }

        // Concurrent encoder for decode uses explicit per-resource barriers via DISPATCH_BARRIER().

        // Check for fusion group at this node
        if let Some(group) = fusion_at.get(&idx) {
            match group {
                FusionGroup::QKV { q, k, v } => {
                    let q_node = &graph.nodes[*q];
                    let k_node = &graph.nodes[*k];
                    let v_node = &graph.nodes[*v];
                    src.push_str(&format!("    // Fused QKV: {:?}, {:?}, {:?}\n", q_node.id, k_node.id, v_node.id));
                    src.push_str("    if (seq_len > 1) {\n");
                    // Prefill: 3 separate GEMMs
                    for n in [q_node, k_node, v_node] {
                        if let Some(ka) = &n.kernel {
                            if let ExecutionPath::Dual { prefill_kernel_name, prefill_dispatch, .. } = &ka.path {
                                src.push_str(&emit_buffer_bindings(n, graph, memory_plan, &act_index, "        "));
                                src.push_str(&emit_op_params(n, graph, "        "));
                                src.push_str(&emit_dispatch_indented(prefill_kernel_name, prefill_dispatch, "seq_len", "        "));
                            }
                        }
                    }
                    src.push_str("    } else {\n");
                    // Decode: check for PSQ reader (inline RMSNorm from partial sums)
                    if let Some(psq_r) = psq_readers.get(&idx).cloned() {
                        let p = &graph.metadata.params;
                        // A = un-normalized residual sum (add_output)
                        let (a_buf, a_off) = resolve_buffer(psq_r.activation_tid, graph, memory_plan, &act_index, true);
                        // QKV outputs
                        let (oq_buf, oq_off) = resolve_buffer(q_node.output, graph, memory_plan, &act_index, true);
                        let (ok_buf, ok_off) = resolve_buffer(k_node.output, graph, memory_plan, &act_index, true);
                        let (ov_buf, ov_off) = resolve_buffer(v_node.output, graph, memory_plan, &act_index, true);
                        // Norm weight
                        let (nw_buf, nw_off) = resolve_buffer(psq_r.norm_weight_tid, graph, memory_plan, &act_index, true);
                        // Quant weights
                        let resolve_qw = |n: &Node| -> (String, usize) {
                            let t = graph.tensors.get(&n.inputs[1]).unwrap();
                            if let StorageClass::Weight(w) = &t.storage {
                                ("s->quant_weight_buf".to_string(), quant_map.get(&w.byte_offset).copied().unwrap_or(0))
                            } else {
                                let (b, o) = resolve_buffer(n.inputs[1], graph, memory_plan, &act_index, true);
                                (b.to_string(), o)
                            }
                        };
                        let (wq_buf, wq_off) = resolve_qw(q_node);
                        let (wk_buf, wk_off) = resolve_qw(k_node);
                        let (wv_buf, wv_off) = resolve_qw(v_node);

                        let m_q = graph.tensors.get(&q_node.output)
                            .map(|t| t.shape.max_shape()).unwrap_or_default()
                            .first().copied().unwrap_or(p.hidden_size) as u32;
                        let m_kv = graph.tensors.get(&k_node.output)
                            .map(|t| t.shape.max_shape()).unwrap_or_default()
                            .first().copied().unwrap_or(p.num_kv_heads * p.head_dim) as u32;
                        let k_dim = graph.tensors.get(&q_node.inputs[1])
                            .map(|t| t.shape.max_shape()).unwrap_or_default()
                            .last().copied().unwrap_or(p.hidden_size) as u32;
                        let eps_bits = psq_r.eps.to_bits();
                        let num_psq = psq_r.num_psq_tgs;

                        src.push_str("        // PSQ reader: QKV GEMV with inline RMSNorm from partial sums\n");
                        src.push_str(&format!("        [enc setBuffer:{oq_buf} offset:{} atIndex:0];\n", format_offset(&oq_buf, oq_off)));
                        src.push_str(&format!("        [enc setBuffer:{a_buf} offset:{} atIndex:1];\n", format_offset(&a_buf, a_off)));
                        src.push_str(&format!("        [enc setBuffer:{wq_buf} offset:{wq_off} atIndex:2];\n"));
                        src.push_str(&format!("        [enc setBuffer:{ok_buf} offset:{} atIndex:3];\n", format_offset(&ok_buf, ok_off)));
                        src.push_str(&format!("        [enc setBuffer:{wk_buf} offset:{wk_off} atIndex:4];\n"));
                        src.push_str(&format!("        [enc setBuffer:{ov_buf} offset:{} atIndex:5];\n", format_offset(&ov_buf, ov_off)));
                        src.push_str(&format!("        [enc setBuffer:{wv_buf} offset:{wv_off} atIndex:6];\n"));
                        src.push_str("        [enc setBuffer:s->psq_buf offset:0 atIndex:7];\n");
                        src.push_str(&format!("        [enc setBuffer:{nw_buf} offset:{} atIndex:8];\n", format_offset(&nw_buf, nw_off)));
                        src.push_str(&format!(
                            "        {{ uint32_t _p[8] = {{1, act_stride, {k_dim}, {m_q}, {m_kv}, {eps_bits}, {num_psq}, 0}};\n\
                             \x20         [enc setBytes:_p length:32 atIndex:9]; }}\n"
                        ));
                        let grid_x = (m_q + 15) / 16;
                        src.push_str("        DISPATCH_BARRIER();\n");
                        src.push_str(&format!("        [enc setComputePipelineState:pso_fused_qkv_norm_qmv_fast_{}];\n", q4_suffix));
                        src.push_str(&format!("        [enc dispatchThreadgroups:MTLSizeMake({grid_x}, 1, 1) threadsPerThreadgroup:MTLSizeMake(128, 1, 1)];\n"));
                    } else if let Some(sf) = norm_gemv_super.get(&idx).cloned() {
                    // Decode: check for super-fused Add+RMSNorm+QKV
                        let add_node_sf = &graph.nodes[sf.add_node_idx];
                        let norm_node_sf = &graph.nodes[sf.norm_node_idx];
                        let p = &graph.metadata.params;

                        // Add inputs (residual + delta)
                        let (x_buf, x_off) = resolve_buffer(add_node_sf.inputs[0], graph, memory_plan, &act_index, true);
                        let (delta_buf, delta_off) = resolve_buffer(add_node_sf.inputs[1], graph, memory_plan, &act_index, true);
                        let (res_buf, res_off) = resolve_buffer(add_node_sf.output, graph, memory_plan, &act_index, true);
                        let (nw_buf, nw_off) = resolve_buffer(sf.norm_weight_tid, graph, memory_plan, &act_index, true);

                        // QKV outputs
                        let (oq_buf, oq_off) = resolve_buffer(q_node.output, graph, memory_plan, &act_index, true);
                        let (ok_buf, ok_off) = resolve_buffer(k_node.output, graph, memory_plan, &act_index, true);
                        let (ov_buf, ov_off) = resolve_buffer(v_node.output, graph, memory_plan, &act_index, true);

                        // Quant weights
                        let resolve_qw = |n: &Node| -> (String, usize) {
                            let t = graph.tensors.get(&n.inputs[1]).unwrap();
                            if let StorageClass::Weight(w) = &t.storage {
                                ("s->quant_weight_buf".to_string(), quant_map.get(&w.byte_offset).copied().unwrap_or(0))
                            } else {
                                let (b, o) = resolve_buffer(n.inputs[1], graph, memory_plan, &act_index, true);
                                (b.to_string(), o)
                            }
                        };
                        let (wq_buf, wq_off) = resolve_qw(q_node);
                        let (wk_buf, wk_off) = resolve_qw(k_node);
                        let (wv_buf, wv_off) = resolve_qw(v_node);

                        let m_q = graph.tensors.get(&q_node.output)
                            .map(|t| t.shape.max_shape()).unwrap_or_default()
                            .first().copied().unwrap_or(p.hidden_size) as u32;
                        let m_kv = graph.tensors.get(&k_node.output)
                            .map(|t| t.shape.max_shape()).unwrap_or_default()
                            .first().copied().unwrap_or(p.num_kv_heads * p.head_dim) as u32;
                        let k_dim = graph.tensors.get(&q_node.inputs[1])
                            .map(|t| t.shape.max_shape()).unwrap_or_default()
                            .last().copied().unwrap_or(p.hidden_size) as u32;
                        let eps = match &norm_node_sf.op {
                            Op::RMSNorm { eps } => *eps,
                            _ => 1e-5,
                        };
                        let eps_bits = (eps as f32).to_bits();

                        src.push_str("        // Super-fused Add+RMSNorm+QKV decode\n");
                        src.push_str(&format!("        [enc setBuffer:{oq_buf} offset:{} atIndex:0];\n", format_offset(&oq_buf, oq_off)));
                        src.push_str(&format!("        [enc setBuffer:{x_buf} offset:{} atIndex:1];\n", format_offset(&x_buf, x_off)));
                        src.push_str(&format!("        [enc setBuffer:{delta_buf} offset:{} atIndex:2];\n", format_offset(&delta_buf, delta_off)));
                        src.push_str(&format!("        [enc setBuffer:{nw_buf} offset:{} atIndex:3];\n", format_offset(&nw_buf, nw_off)));
                        src.push_str(&format!("        [enc setBuffer:{wq_buf} offset:{wq_off} atIndex:4];\n"));
                        src.push_str(&format!("        [enc setBuffer:{ok_buf} offset:{} atIndex:5];\n", format_offset(&ok_buf, ok_off)));
                        src.push_str(&format!("        [enc setBuffer:{wk_buf} offset:{wk_off} atIndex:6];\n"));
                        src.push_str(&format!("        [enc setBuffer:{ov_buf} offset:{} atIndex:7];\n", format_offset(&ov_buf, ov_off)));
                        src.push_str(&format!("        [enc setBuffer:{wv_buf} offset:{wv_off} atIndex:8];\n"));
                        src.push_str(&format!("        [enc setBuffer:{res_buf} offset:{} atIndex:9];\n", format_offset(&res_buf, res_off)));
                        src.push_str(&format!(
                            "        {{ uint32_t _p[8] = {{{k_dim}, {m_q}, {m_kv}, {eps_bits}, 1, 0, 0, 0}};\n\
                             \x20         [enc setBytes:_p length:32 atIndex:10]; }}\n"
                        ));
                        let grid_x = (m_q + 15) / 16;
                        src.push_str("        DISPATCH_BARRIER();\n");
                        src.push_str(&format!("        [enc setComputePipelineState:pso_fused_add_rmsnorm_qkv_qmv_fast_{}];\n", q4_suffix));
                        src.push_str("        [enc setThreadgroupMemoryLength:16 atIndex:0];\n");
                        src.push_str(&format!("        [enc dispatchThreadgroups:MTLSizeMake({grid_x}, 1, 1) threadsPerThreadgroup:MTLSizeMake(128, 1, 1)];\n"));
                    } else {
                        // Regular fused QKV GEMV
                        src.push_str(&emit_fused_qkv_decode(
                            q_node, k_node, v_node, graph, memory_plan, &act_index, quant, quant_map, "        ",
                        ));
                    }
                    src.push_str("    }\n\n");
                }
                FusionGroup::GateUp { gate, up } => {
                    let gate_node = &graph.nodes[*gate];
                    let up_node = &graph.nodes[*up];

                    // Check for three-way Gate+Up+SwiGLU fusion
                    let swiglu_out = gate_to_swiglu.get(&gate_node.output).cloned();

                    if let Some((swiglu_out_tid, silu_idx)) = &swiglu_out {
                        src.push_str(&format!("    // Fused Gate+Up+SwiGLU: {:?}, {:?} → {:?}\n", gate_node.id, up_node.id, swiglu_out_tid));
                        // Skip the SiLU node dispatch (already in skip_nodes via swiglu_at)
                        skip_nodes.insert(*silu_idx);
                    } else {
                        src.push_str(&format!("    // Fused Gate+Up: {:?}, {:?}\n", gate_node.id, up_node.id));
                    }

                    src.push_str("    if (seq_len > 1) {\n");
                    // Prefill: 2 separate GEMMs (+ separate SwiGLU dispatch if fused)
                    for n in [gate_node, up_node] {
                        if let Some(ka) = &n.kernel {
                            if let ExecutionPath::Dual { prefill_kernel_name, prefill_dispatch, .. } = &ka.path {
                                src.push_str(&emit_buffer_bindings(n, graph, memory_plan, &act_index, "        "));
                                src.push_str(&emit_op_params(n, graph, "        "));
                                src.push_str(&emit_dispatch_indented(prefill_kernel_name, prefill_dispatch, "seq_len", "        "));
                            }
                        }
                    }
                    if let Some((_swiglu_out_tid, silu_idx)) = &swiglu_out {
                        // Prefill still needs separate SwiGLU dispatch
                        let swiglu_fusion = swiglu_at.get(silu_idx).unwrap();
                        let silu_node = &graph.nodes[*silu_idx];
                        let mul_node = &graph.nodes[swiglu_fusion.mul_idx];
                        let (out_buf, out_off) = resolve_buffer(mul_node.output, graph, memory_plan, &act_index, false);
                        let (gate_buf, gate_off) = resolve_buffer(silu_node.inputs[0], graph, memory_plan, &act_index, false);
                        let (up_buf, up_off) = resolve_buffer(swiglu_fusion.other_input, graph, memory_plan, &act_index, false);
                        let out_off_e = format_offset(&out_buf, out_off);
                        let gate_off_e = format_offset(&gate_buf, gate_off);
                        let up_off_e = format_offset(&up_buf, up_off);
                        let h = graph.tensors.get(&mul_node.output)
                            .map(|t| t.shape.max_shape()).unwrap_or_default()
                            .first().copied().unwrap_or(graph.metadata.params.intermediate_size) as u32;
                        src.push_str(&format!("        [enc setBuffer:{out_buf} offset:{out_off_e} atIndex:0];\n"));
                        src.push_str(&format!("        [enc setBuffer:{gate_buf} offset:{gate_off_e} atIndex:1];\n"));
                        src.push_str(&format!("        [enc setBuffer:{up_buf} offset:{up_off_e} atIndex:2];\n"));
                        src.push_str(&format!(
                            "        {{ uint32_t _p[8] = {{seq_len, act_stride, {h}, 0, 0, 0, 0, 0}};\n\
                             \x20         [enc setBytes:_p length:32 atIndex:4]; }}\n"
                        ));
                        let grid_x = (h + 255) / 256;
                        src.push_str("        DISPATCH_BARRIER();\n");
                        src.push_str("        [enc setComputePipelineState:pso_swiglu_f16];\n");
                        src.push_str(&format!("        [enc dispatchThreadgroups:MTLSizeMake({grid_x}, 1, 1) threadsPerThreadgroup:MTLSizeMake(256, 1, 1)];\n"));
                    }
                    src.push_str("    } else {\n");
                    let quant_active = matches!(quant, "q8_0" | "q4_0" | "q4_mlx");
                    // Check for PSQ reader: GateUp+SwiGLU with inline RMSNorm from partial sums
                    if let Some(psq_r) = swiglu_out.as_ref().and_then(|_| psq_readers.get(&idx).cloned()) {
                        let p = &graph.metadata.params;
                        let (swiglu_out_tid, _) = swiglu_out.as_ref().unwrap();
                        // A = un-normalized residual sum (add_output)
                        let (a_buf, a_off) = resolve_buffer(psq_r.activation_tid, graph, memory_plan, &act_index, true);
                        let (out_buf, out_off) = resolve_buffer(*swiglu_out_tid, graph, memory_plan, &act_index, true);
                        let (nw_buf, nw_off) = resolve_buffer(psq_r.norm_weight_tid, graph, memory_plan, &act_index, true);
                        let resolve_qw = |n: &Node| -> (String, usize) {
                            let t = graph.tensors.get(&n.inputs[1]).unwrap();
                            if let StorageClass::Weight(w) = &t.storage {
                                ("s->quant_weight_buf".to_string(), quant_map.get(&w.byte_offset).copied().unwrap_or(0))
                            } else {
                                let (b, o) = resolve_buffer(n.inputs[1], graph, memory_plan, &act_index, true);
                                (b.to_string(), o)
                            }
                        };
                        let (wg_buf, wg_off) = resolve_qw(gate_node);
                        let (wu_buf, wu_off) = resolve_qw(up_node);

                        let m = graph.tensors.get(&gate_node.output)
                            .map(|t| t.shape.max_shape()).unwrap_or_default()
                            .first().copied().unwrap_or(p.intermediate_size) as u32;
                        let k_dim = graph.tensors.get(&gate_node.inputs[1])
                            .map(|t| t.shape.max_shape()).unwrap_or_default()
                            .last().copied().unwrap_or(p.hidden_size) as u32;
                        let eps_bits = psq_r.eps.to_bits();
                        let num_psq = psq_r.num_psq_tgs;

                        src.push_str("        // PSQ reader: GateUp+SwiGLU GEMV with inline RMSNorm from partial sums\n");
                        src.push_str(&format!("        [enc setBuffer:{out_buf} offset:{} atIndex:0];\n", format_offset(&out_buf, out_off)));
                        src.push_str(&format!("        [enc setBuffer:{a_buf} offset:{} atIndex:1];\n", format_offset(&a_buf, a_off)));
                        src.push_str(&format!("        [enc setBuffer:{wg_buf} offset:{wg_off} atIndex:2];\n"));
                        src.push_str(&format!("        [enc setBuffer:{wu_buf} offset:{wu_off} atIndex:3];\n"));
                        src.push_str("        [enc setBuffer:s->psq_buf offset:0 atIndex:4];\n");
                        src.push_str(&format!("        [enc setBuffer:{nw_buf} offset:{} atIndex:5];\n", format_offset(&nw_buf, nw_off)));
                        src.push_str(&format!(
                            "        {{ uint32_t _p[8] = {{1, act_stride, {k_dim}, {m}, {eps_bits}, {num_psq}, 0, 0}};\n\
                             \x20         [enc setBytes:_p length:32 atIndex:6]; }}\n"
                        ));
                        let grid_x = (m + 15) / 16;
                        src.push_str("        DISPATCH_BARRIER();\n");
                        src.push_str(&format!("        [enc setComputePipelineState:pso_fused_gate_up_swiglu_norm_qmv_fast_{}];\n", q4_suffix));
                        src.push_str(&format!("        [enc dispatchThreadgroups:MTLSizeMake({grid_x}, 1, 1) threadsPerThreadgroup:MTLSizeMake(128, 1, 1)];\n"));
                    } else if let Some(sf) = swiglu_out.as_ref().and_then(|_| norm_gemv_super.get(&idx).cloned()) {
                        // Decode: super-fused Add+RMSNorm+GateUp+SwiGLU
                        let add_node_sf = &graph.nodes[sf.add_node_idx];
                        let norm_node_sf = &graph.nodes[sf.norm_node_idx];
                        let p = &graph.metadata.params;
                        let (swiglu_out_tid, _) = swiglu_out.as_ref().unwrap();

                        let (x_buf, x_off) = resolve_buffer(add_node_sf.inputs[0], graph, memory_plan, &act_index, true);
                        let (delta_buf, delta_off) = resolve_buffer(add_node_sf.inputs[1], graph, memory_plan, &act_index, true);
                        let (res_buf, res_off) = resolve_buffer(add_node_sf.output, graph, memory_plan, &act_index, true);
                        let (nw_buf, nw_off) = resolve_buffer(sf.norm_weight_tid, graph, memory_plan, &act_index, true);
                        let (out_buf, out_off) = resolve_buffer(*swiglu_out_tid, graph, memory_plan, &act_index, true);

                        let resolve_qw = |n: &Node| -> (String, usize) {
                            let t = graph.tensors.get(&n.inputs[1]).unwrap();
                            if let StorageClass::Weight(w) = &t.storage {
                                ("s->quant_weight_buf".to_string(), quant_map.get(&w.byte_offset).copied().unwrap_or(0))
                            } else {
                                let (b, o) = resolve_buffer(n.inputs[1], graph, memory_plan, &act_index, true);
                                (b.to_string(), o)
                            }
                        };
                        let (wg_buf, wg_off) = resolve_qw(gate_node);
                        let (wu_buf, wu_off) = resolve_qw(up_node);

                        let m = graph.tensors.get(&gate_node.output)
                            .map(|t| t.shape.max_shape()).unwrap_or_default()
                            .first().copied().unwrap_or(p.intermediate_size) as u32;
                        let k_dim = graph.tensors.get(&gate_node.inputs[1])
                            .map(|t| t.shape.max_shape()).unwrap_or_default()
                            .last().copied().unwrap_or(p.hidden_size) as u32;
                        let eps = match &norm_node_sf.op {
                            Op::RMSNorm { eps } => *eps,
                            _ => 1e-5,
                        };
                        let eps_bits = (eps as f32).to_bits();

                        src.push_str("        // Super-fused Add+RMSNorm+GateUp+SwiGLU decode\n");
                        src.push_str(&format!("        [enc setBuffer:{out_buf} offset:{} atIndex:0];\n", format_offset(&out_buf, out_off)));
                        src.push_str(&format!("        [enc setBuffer:{x_buf} offset:{} atIndex:1];\n", format_offset(&x_buf, x_off)));
                        src.push_str(&format!("        [enc setBuffer:{delta_buf} offset:{} atIndex:2];\n", format_offset(&delta_buf, delta_off)));
                        src.push_str(&format!("        [enc setBuffer:{nw_buf} offset:{} atIndex:3];\n", format_offset(&nw_buf, nw_off)));
                        src.push_str(&format!("        [enc setBuffer:{wg_buf} offset:{wg_off} atIndex:4];\n"));
                        src.push_str(&format!("        [enc setBuffer:{wu_buf} offset:{wu_off} atIndex:5];\n"));
                        src.push_str(&format!("        [enc setBuffer:{res_buf} offset:{} atIndex:6];\n", format_offset(&res_buf, res_off)));
                        src.push_str(&format!(
                            "        {{ uint32_t _p[8] = {{{k_dim}, {m}, {eps_bits}, 0, 0, 0, 0, 0}};\n\
                             \x20         [enc setBytes:_p length:32 atIndex:7]; }}\n"
                        ));
                        let grid_x = (m + 15) / 16;
                        src.push_str("        DISPATCH_BARRIER();\n");
                        src.push_str(&format!("        [enc setComputePipelineState:pso_fused_add_rmsnorm_gate_up_swiglu_qmv_fast_{}];\n", q4_suffix));
                        src.push_str(&format!("        [enc dispatchThreadgroups:MTLSizeMake({grid_x}, 1, 1) threadsPerThreadgroup:MTLSizeMake(128, 1, 1)];\n"));
                    } else if swiglu_out.is_some() && quant_active {
                        // Decode (quantized): fused Gate+Up+SwiGLU GEMV
                        let (swiglu_out_tid, _) = swiglu_out.as_ref().unwrap();
                        src.push_str(&emit_fused_gate_up_swiglu_decode(
                            gate_node, up_node, *swiglu_out_tid, graph, memory_plan, &act_index, quant, quant_map, "        ",
                        ));
                    } else if swiglu_out.is_some() {
                        // Decode (F16): fused Gate+Up GEMV + separate SwiGLU
                        src.push_str(&emit_fused_gate_up_decode(
                            gate_node, up_node, graph, memory_plan, &act_index, quant, quant_map, "        ",
                        ));
                        // Emit separate SwiGLU dispatch
                        let (_, silu_idx) = swiglu_out.as_ref().unwrap();
                        let swiglu_fusion = swiglu_at.get(silu_idx).unwrap();
                        let silu_node = &graph.nodes[*silu_idx];
                        let mul_node = &graph.nodes[swiglu_fusion.mul_idx];
                        let (out_buf, out_off) = resolve_buffer(mul_node.output, graph, memory_plan, &act_index, true);
                        let (gate_buf, gate_off) = resolve_buffer(silu_node.inputs[0], graph, memory_plan, &act_index, true);
                        let (up_buf, up_off) = resolve_buffer(swiglu_fusion.other_input, graph, memory_plan, &act_index, true);
                        let out_off_e = format_offset(&out_buf, out_off);
                        let gate_off_e = format_offset(&gate_buf, gate_off);
                        let up_off_e = format_offset(&up_buf, up_off);
                        let h = graph.tensors.get(&mul_node.output)
                            .map(|t| t.shape.max_shape()).unwrap_or_default()
                            .first().copied().unwrap_or(graph.metadata.params.intermediate_size) as u32;
                        src.push_str(&format!("        [enc setBuffer:{out_buf} offset:{out_off_e} atIndex:0];\n"));
                        src.push_str(&format!("        [enc setBuffer:{gate_buf} offset:{gate_off_e} atIndex:1];\n"));
                        src.push_str(&format!("        [enc setBuffer:{up_buf} offset:{up_off_e} atIndex:2];\n"));
                        src.push_str(&format!(
                            "        {{ uint32_t _p[8] = {{seq_len, act_stride, {h}, 0, 0, 0, 0, 0}};\n\
                             \x20         [enc setBytes:_p length:32 atIndex:4]; }}\n"
                        ));
                        let grid_x = (h + 255) / 256;
                        src.push_str("        DISPATCH_BARRIER();\n");
                        src.push_str("        [enc setComputePipelineState:pso_swiglu_f16];\n");
                        src.push_str(&format!("        [enc dispatchThreadgroups:MTLSizeMake({grid_x}, 1, 1) threadsPerThreadgroup:MTLSizeMake(256, 1, 1)];\n"));
                    } else {
                        // Decode: fused Gate+Up GEMV (no SwiGLU)
                        src.push_str(&emit_fused_gate_up_decode(
                            gate_node, up_node, graph, memory_plan, &act_index, quant, quant_map, "        ",
                        ));
                    }
                    src.push_str("    }\n\n");
                }
            }
            continue;
        }

        // Check for SwiGLU fusion: SiLU node replaced with swiglu_f16
        if let Some(swiglu) = swiglu_at.get(&idx) {
            let mul_node = &graph.nodes[swiglu.mul_idx];
            let silu_input = node.inputs[0]; // gate input
            let up_input = swiglu.other_input;

            let (out_buf, out_off) = resolve_buffer(mul_node.output, graph, memory_plan, &act_index, false);
            let (gate_buf, gate_off) = resolve_buffer(silu_input, graph, memory_plan, &act_index, false);
            let (up_buf, up_off) = resolve_buffer(up_input, graph, memory_plan, &act_index, false);

            let out_off_e = format_offset(&out_buf, out_off);
            let gate_off_e = format_offset(&gate_buf, gate_off);
            let up_off_e = format_offset(&up_buf, up_off);

            // h = first dim of output shape (static hidden dimension)
            let h = graph.tensors.get(&mul_node.output)
                .map(|t| t.shape.max_shape())
                .unwrap_or_default()
                .first().copied()
                .unwrap_or(graph.metadata.params.intermediate_size) as u32;

            let grid_x = (h + 255) / 256;
            src.push_str(&format!("    // SwiGLU fusion: SiLU({:?}) * {:?} → {:?}\n", node.id, up_input, mul_node.id));
            src.push_str("    if (seq_len > 1) {\n");
            src.push_str(&format!("        [enc setBuffer:{out_buf} offset:{out_off_e} atIndex:0];\n"));
            src.push_str(&format!("        [enc setBuffer:{gate_buf} offset:{gate_off_e} atIndex:1];\n"));
            src.push_str(&format!("        [enc setBuffer:{up_buf} offset:{up_off_e} atIndex:2];\n"));
            src.push_str(&format!(
                "        {{ uint32_t _p[8] = {{seq_len, act_stride, {h}, 0, 0, 0, 0, 0}};\n\
                 \x20         [enc setBytes:_p length:32 atIndex:4]; }}\n"
            ));
            src.push_str("        DISPATCH_BARRIER();\n");
            src.push_str("        [enc setComputePipelineState:pso_swiglu_f16];\n");
            src.push_str(&format!("        [enc dispatchThreadgroups:MTLSizeMake({grid_x}, 1, 1) threadsPerThreadgroup:MTLSizeMake(256, 1, 1)];\n"));
            src.push_str("    } else {\n");
            {
                let (d_out_buf, d_out_off) = resolve_buffer(mul_node.output, graph, memory_plan, &act_index, true);
                let (d_gate_buf, d_gate_off) = resolve_buffer(silu_input, graph, memory_plan, &act_index, true);
                let (d_up_buf, d_up_off) = resolve_buffer(up_input, graph, memory_plan, &act_index, true);
                src.push_str(&format!("        [enc setBuffer:{d_out_buf} offset:{d_out_off} atIndex:0];\n"));
                src.push_str(&format!("        [enc setBuffer:{d_gate_buf} offset:{d_gate_off} atIndex:1];\n"));
                src.push_str(&format!("        [enc setBuffer:{d_up_buf} offset:{d_up_off} atIndex:2];\n"));
            }
            src.push_str(&format!(
                "        {{ uint32_t _p[8] = {{seq_len, act_stride, {h}, 0, 0, 0, 0, 0}};\n\
                 \x20         [enc setBytes:_p length:32 atIndex:4]; }}\n"
            ));
            src.push_str("        DISPATCH_BARRIER();\n");
            src.push_str("        [enc setComputePipelineState:pso_swiglu_f16];\n");
            src.push_str(&format!("        [enc dispatchThreadgroups:MTLSizeMake({grid_x}, 1, 1) threadsPerThreadgroup:MTLSizeMake(256, 1, 1)];\n"));
            src.push_str("    }\n\n");
            continue;
        }

        // Check for Add+RMSNorm fusion
        if let Some(fusion) = add_rmsnorm_at.get(&idx) {
            let norm_node = &graph.nodes[fusion.norm_idx];
            let p = &graph.metadata.params;

            // Add inputs: x (residual) and delta (projection output)
            let (x_buf, x_off) = resolve_buffer(node.inputs[0], graph, memory_plan, &act_index, false);
            let (delta_buf, delta_off) = resolve_buffer(node.inputs[1], graph, memory_plan, &act_index, false);
            // Add output = residual sum (used as next residual)
            let (res_buf, res_off) = resolve_buffer(node.output, graph, memory_plan, &act_index, false);
            // Norm output
            let (norm_out_buf, norm_out_off) = resolve_buffer(norm_node.output, graph, memory_plan, &act_index, false);
            // Norm weight
            let (wt_buf, wt_off) = resolve_buffer(fusion.norm_weight_input, graph, memory_plan, &act_index, false);

            let x_off_e = format_offset(&x_buf, x_off);
            let delta_off_e = format_offset(&delta_buf, delta_off);
            let res_off_e = format_offset(&res_buf, res_off);
            let norm_out_off_e = format_offset(&norm_out_buf, norm_out_off);
            let wt_off_e = format_offset(&wt_buf, wt_off);

            let h = p.hidden_size as u32;
            let eps = match &norm_node.op {
                Op::RMSNorm { eps } => *eps,
                _ => 1e-5,
            };
            let eps_bits = (eps as f32).to_bits();

            let is_superfused = add_has_superfusion.contains_key(&idx) && (quant == "q4_0" || quant == "q4_mlx");
            let is_psq = psq_skip_add.contains(&idx);
            let skip_decode = is_superfused || is_psq;
            src.push_str(&format!("    // Fused Add+RMSNorm: {:?} + {:?}{}\n", node.id, norm_node.id,
                if is_psq { " (decode handled by PSQ pipeline)" }
                else if is_superfused { " (decode handled by super-fused kernel)" }
                else { "" }));
            // All cases (skip_decode or not) now split into if/else for region buffers
            src.push_str("    if (seq_len > 1) {\n");
            src.push_str(&format!("        [enc setBuffer:{norm_out_buf} offset:{norm_out_off_e} atIndex:0];\n"));
            src.push_str(&format!("        [enc setBuffer:{x_buf} offset:{x_off_e} atIndex:1];\n"));
            src.push_str(&format!("        [enc setBuffer:{delta_buf} offset:{delta_off_e} atIndex:2];\n"));
            src.push_str(&format!("        [enc setBuffer:{wt_buf} offset:{wt_off_e} atIndex:3];\n"));
            src.push_str(&format!(
                "        {{ uint32_t _p[8] = {{seq_len, act_stride, {h}, {eps_bits}, 0, 0, 0, 0}};\n\
                 \x20         [enc setBytes:_p length:32 atIndex:4]; }}\n"
            ));
            src.push_str(&format!("        [enc setBuffer:{res_buf} offset:{res_off_e} atIndex:5];\n"));
            src.push_str("        DISPATCH_BARRIER();\n");
            src.push_str("        [enc setComputePipelineState:pso_add_rms_norm_f16];\n");
            src.push_str("        [enc setThreadgroupMemoryLength:1024 atIndex:0];\n");
            src.push_str("        [enc dispatchThreadgroups:MTLSizeMake(seq_len, 1, 1) threadsPerThreadgroup:MTLSizeMake(256, 1, 1)];\n");
            if skip_decode {
                src.push_str("    }\n");
            } else {
                src.push_str("    } else {\n");
                let (d_norm_buf, d_norm_off) = resolve_buffer(norm_node.output, graph, memory_plan, &act_index, true);
                let (d_x_buf, d_x_off) = resolve_buffer(node.inputs[0], graph, memory_plan, &act_index, true);
                let (d_delta_buf, d_delta_off) = resolve_buffer(node.inputs[1], graph, memory_plan, &act_index, true);
                let (d_res_buf, d_res_off) = resolve_buffer(node.output, graph, memory_plan, &act_index, true);
                let (d_wt_buf, d_wt_off) = resolve_buffer(fusion.norm_weight_input, graph, memory_plan, &act_index, true);
                src.push_str(&format!("        [enc setBuffer:{d_norm_buf} offset:{d_norm_off} atIndex:0];\n"));
                src.push_str(&format!("        [enc setBuffer:{d_x_buf} offset:{d_x_off} atIndex:1];\n"));
                src.push_str(&format!("        [enc setBuffer:{d_delta_buf} offset:{d_delta_off} atIndex:2];\n"));
                src.push_str(&format!("        [enc setBuffer:{d_wt_buf} offset:{} atIndex:3];\n", d_wt_off));
                src.push_str(&format!(
                    "        {{ uint32_t _p[8] = {{seq_len, act_stride, {h}, {eps_bits}, 0, 0, 0, 0}};\n\
                     \x20         [enc setBytes:_p length:32 atIndex:4]; }}\n"
                ));
                src.push_str(&format!("        [enc setBuffer:{d_res_buf} offset:{d_res_off} atIndex:5];\n"));
                src.push_str("        DISPATCH_BARRIER();\n");
                src.push_str("        [enc setComputePipelineState:pso_add_rms_norm_f16];\n");
                src.push_str("        [enc setThreadgroupMemoryLength:1024 atIndex:0];\n");
                src.push_str("        [enc dispatchThreadgroups:MTLSizeMake(1, 1, 1) threadsPerThreadgroup:MTLSizeMake(256, 1, 1)];\n");
                src.push_str("    }\n");
            }
            src.push_str("\n");
            continue;
        }

        // Check for RoPE Q+K + KV Append super-fusion
        if let Some(rkv) = rope_kv_at.get(&idx) {
            let rope = rope_qk_at.get(&idx).unwrap();
            let k_rope_node = &graph.nodes[rope.k_idx];
            let kv_k_node = &graph.nodes[rkv.kv_k_idx];
            let kv_v_node = &graph.nodes[rkv.kv_v_idx];
            let p = &graph.metadata.params;

            // RoPE Q: out_q (activation buf), q_in (activation buf)
            let (out_q_buf, out_q_off) = resolve_buffer(node.output, graph, memory_plan, &act_index, false);
            let (q_buf, q_off) = resolve_buffer(node.inputs[0], graph, memory_plan, &act_index, false);
            // K cache + K input (raw, pre-RoPE)
            let (k_cache_buf, k_cache_off) = resolve_buffer(kv_k_node.output, graph, memory_plan, &act_index, false);
            let (k_buf, k_off) = resolve_buffer(k_rope_node.inputs[0], graph, memory_plan, &act_index, false);
            // V cache + V input (raw)
            let (v_cache_buf, v_cache_off) = resolve_buffer(kv_v_node.output, graph, memory_plan, &act_index, false);
            let (v_buf, v_off) = resolve_buffer(kv_v_node.inputs[1], graph, memory_plan, &act_index, false);

            let out_q_off_e = format_offset(&out_q_buf, out_q_off);
            let q_off_e = format_offset(&q_buf, q_off);
            let k_cache_off_e = format_offset(&k_cache_buf, k_cache_off);
            let k_off_e = format_offset(&k_buf, k_off);
            let v_cache_off_e = format_offset(&v_cache_buf, v_cache_off);
            let v_off_e = format_offset(&v_buf, v_off);

            let rope_dim_q = (p.num_attention_heads * p.head_dim) as u32;
            let rope_dim_k = (p.num_kv_heads * p.head_dim) as u32;
            let head_dim = p.head_dim as u32;
            let max_seq = p.max_position_embeddings as u32;
            let base = match &node.op {
                Op::RoPE { base, .. } => (*base as f32).to_bits(),
                _ => (10000.0f32).to_bits(),
            };

            src.push_str(&format!("    // Fused RoPE Q+K + KV Append: {:?}, {:?}, {:?}, {:?}\n",
                node.id, k_rope_node.id, kv_k_node.id, kv_v_node.id));
            // Prefill path: use separate kernels (RoPE QK + KV Append) since grid > 1
            src.push_str("    if (seq_len > 1) {\n");
            {
                // RoPE QK
                let (tmp_k_buf, tmp_k_off) = resolve_buffer(k_rope_node.output, graph, memory_plan, &act_index, false);
                let out_k_off_e = format_offset(&tmp_k_buf, tmp_k_off);
                let (out_k_buf, _) = resolve_buffer(k_rope_node.output, graph, memory_plan, &act_index, false);
                src.push_str(&format!("        [enc setBuffer:{out_q_buf} offset:{out_q_off_e} atIndex:0];\n"));
                src.push_str(&format!("        [enc setBuffer:{q_buf} offset:{q_off_e} atIndex:1];\n"));
                src.push_str(&format!("        [enc setBuffer:{out_k_buf} offset:{out_k_off_e} atIndex:2];\n"));
                src.push_str(&format!("        [enc setBuffer:{k_buf} offset:{k_off_e} atIndex:3];\n"));
                src.push_str(&format!(
                    "        {{ uint32_t _p[8] = {{seq_len, act_stride, {rope_dim_q}, {head_dim}, 0, {base}, total_seq_len, {rope_dim_k}}};\n\
                     \x20         [enc setBytes:_p length:32 atIndex:4]; }}\n"
                ));
                src.push_str("        [enc setBuffer:s->rope_table offset:0 atIndex:5];\n");
                src.push_str("        DISPATCH_BARRIER();\n");
                src.push_str("        [enc setComputePipelineState:pso_rope_qk_f16];\n");
                src.push_str("        [enc dispatchThreadgroups:MTLSizeMake(seq_len, 1, 1) threadsPerThreadgroup:MTLSizeMake(256, 1, 1)];\n");
                // KV Append
                let (new_k_buf, new_k_off) = resolve_buffer(kv_k_node.inputs[1], graph, memory_plan, &act_index, false);
                let new_k_off_e = format_offset(&new_k_buf, new_k_off);
                src.push_str(&format!("        [enc setBuffer:{k_cache_buf} offset:{k_cache_off_e} atIndex:0];\n"));
                src.push_str(&format!("        [enc setBuffer:{new_k_buf} offset:{new_k_off_e} atIndex:1];\n"));
                src.push_str(&format!("        [enc setBuffer:{v_cache_buf} offset:{v_cache_off_e} atIndex:2];\n"));
                src.push_str(&format!("        [enc setBuffer:{v_buf} offset:{v_off_e} atIndex:3];\n"));
                src.push_str(&format!(
                    "        {{ uint32_t _p[8] = {{seq_len, total_seq_len, act_stride, {head_dim}, {max_seq}, {rope_dim_k}, 0, 0}};\n\
                     \x20         [enc setBytes:_p length:32 atIndex:4]; }}\n"
                ));
                src.push_str("        DISPATCH_BARRIER();\n");
                src.push_str("        [enc setComputePipelineState:pso_kv_cache_append_fused_f16];\n");
                src.push_str("        [enc dispatchThreadgroups:MTLSizeMake(seq_len, 1, 1) threadsPerThreadgroup:MTLSizeMake(256, 1, 1)];\n");
            }
            src.push_str("    } else {\n");
            // Re-resolve activation tensors in decode mode (region buffers)
            let (d_out_q_buf, d_out_q_off) = resolve_buffer(node.output, graph, memory_plan, &act_index, true);
            let (d_q_buf, d_q_off) = resolve_buffer(node.inputs[0], graph, memory_plan, &act_index, true);
            let (d_k_buf, d_k_off) = resolve_buffer(k_rope_node.inputs[0], graph, memory_plan, &act_index, true);
            let (d_v_buf, d_v_off) = resolve_buffer(kv_v_node.inputs[1], graph, memory_plan, &act_index, true);
            // KV cache buffers don't change between modes
            if let Some(&sdpa_idx) = rope_sdpa_fuse.get(&idx) {
                // Decode: fused RoPE+KV+SDPA — single dispatch replaces 2
                let sdpa_node = &graph.nodes[sdpa_idx];
                let (attn_out_buf, attn_out_off) = resolve_buffer(sdpa_node.output, graph, memory_plan, &act_index, true);
                let attn_out_off_e = format_offset(&attn_out_buf, attn_out_off);
                let gqa_factor_val = (p.num_attention_heads / p.num_kv_heads) as u32;
                let num_q_heads = p.num_attention_heads as u32;
                let scale_val = match &sdpa_node.op {
                    Op::ScaledDotProductAttention { scale, .. } => *scale,
                    _ => 1.0 / (head_dim as f32).sqrt(),
                };
                let scale_bits = scale_val.to_bits();

                src.push_str("        // Fused RoPE+KV+SDPA decode (1 dispatch instead of 2)\n");
                src.push_str(&format!("        [enc setBuffer:{attn_out_buf} offset:{attn_out_off_e} atIndex:0];\n"));
                src.push_str(&format!("        [enc setBuffer:{d_q_buf} offset:{d_q_off} atIndex:1];\n"));
                src.push_str(&format!("        [enc setBuffer:{d_k_buf} offset:{d_k_off} atIndex:2];\n"));
                src.push_str(&format!("        [enc setBuffer:{d_v_buf} offset:{d_v_off} atIndex:3];\n"));
                src.push_str(&format!("        [enc setBuffer:{k_cache_buf} offset:{k_cache_off_e} atIndex:4];\n"));
                src.push_str(&format!("        [enc setBuffer:{v_cache_buf} offset:{v_cache_off_e} atIndex:5];\n"));
                src.push_str("        [enc setBuffer:s->rope_table offset:0 atIndex:6];\n");
                src.push_str(&format!(
                    "        {{ uint32_t _p[8] = {{{gqa_factor_val}, total_seq_len, {head_dim}, {scale_bits}u, {max_seq}, 0, 0, 0}};\n\
                     \x20         [enc setBytes:_p length:32 atIndex:7]; }}\n"
                ));
                src.push_str("        DISPATCH_BARRIER();\n");
                let sdpa_rope_kv_pso = if head_dim == 128 { "pso_sdpa_rope_kv_decode_f16_128" } else { "pso_sdpa_rope_kv_decode_f16" };
                src.push_str(&format!("        [enc setComputePipelineState:{sdpa_rope_kv_pso}];\n"));
                src.push_str(&format!("        [enc dispatchThreadgroups:MTLSizeMake({num_q_heads}, 1, 1) threadsPerThreadgroup:MTLSizeMake(32, 32, 1)];\n"));
            } else {
                // Decode: fused RoPE+KV only (no SDPA fusion)
                src.push_str(&format!("        [enc setBuffer:{d_out_q_buf} offset:{d_out_q_off} atIndex:0];\n"));
                src.push_str(&format!("        [enc setBuffer:{d_q_buf} offset:{d_q_off} atIndex:1];\n"));
                src.push_str(&format!("        [enc setBuffer:{k_cache_buf} offset:{k_cache_off_e} atIndex:2];\n"));
                src.push_str(&format!("        [enc setBuffer:{d_k_buf} offset:{d_k_off} atIndex:3];\n"));
                src.push_str(&format!("        [enc setBuffer:{v_cache_buf} offset:{v_cache_off_e} atIndex:4];\n"));
                src.push_str(&format!("        [enc setBuffer:{d_v_buf} offset:{d_v_off} atIndex:5];\n"));
                src.push_str(&format!(
                    "        {{ uint32_t _p[8] = {{seq_len, act_stride, {rope_dim_q}, {head_dim}, {base}, total_seq_len, {rope_dim_k}, {max_seq}}};\n\
                     \x20         [enc setBytes:_p length:32 atIndex:6]; }}\n"
                ));
                src.push_str("        [enc setBuffer:s->rope_table offset:0 atIndex:7];\n");
                src.push_str("        DISPATCH_BARRIER();\n");
                src.push_str("        [enc setComputePipelineState:pso_rope_qk_kv_append_f16];\n");
                src.push_str("        [enc dispatchThreadgroups:MTLSizeMake(1, 1, 1) threadsPerThreadgroup:MTLSizeMake(256, 1, 1)];\n");
            }
            src.push_str("    }\n\n");
            continue;
        }

        // Check for RoPE Q+K fusion (standalone, no KV Append super-fusion)
        if let Some(fusion) = rope_qk_at.get(&idx) {
            if rope_kv_at.contains_key(&idx) {
                // Already handled by super-fusion above
                continue;
            }
            let k_node = &graph.nodes[fusion.k_idx];
            let p = &graph.metadata.params;

            let (out_q_buf, out_q_off) = resolve_buffer(node.output, graph, memory_plan, &act_index, false);
            let (q_buf, q_off) = resolve_buffer(node.inputs[0], graph, memory_plan, &act_index, false);
            let (out_k_buf, out_k_off) = resolve_buffer(k_node.output, graph, memory_plan, &act_index, false);
            let (k_buf, k_off) = resolve_buffer(k_node.inputs[0], graph, memory_plan, &act_index, false);

            let out_q_off_e = format_offset(&out_q_buf, out_q_off);
            let q_off_e = format_offset(&q_buf, q_off);
            let out_k_off_e = format_offset(&out_k_buf, out_k_off);
            let k_off_e = format_offset(&k_buf, k_off);

            let rope_dim_q = (p.num_attention_heads * p.head_dim) as u32;
            let rope_dim_k = (p.num_kv_heads * p.head_dim) as u32;
            let head_dim = p.head_dim as u32;
            let base = match &node.op {
                Op::RoPE { base, .. } => (*base as f32).to_bits(),
                _ => (10000.0f32).to_bits(),
            };

            src.push_str(&format!("    // Fused RoPE Q+K: {:?} + {:?}\n", node.id, k_node.id));
            src.push_str("    if (seq_len > 1) {\n");
            src.push_str(&format!("        [enc setBuffer:{out_q_buf} offset:{out_q_off_e} atIndex:0];\n"));
            src.push_str(&format!("        [enc setBuffer:{q_buf} offset:{q_off_e} atIndex:1];\n"));
            src.push_str(&format!("        [enc setBuffer:{out_k_buf} offset:{out_k_off_e} atIndex:2];\n"));
            src.push_str(&format!("        [enc setBuffer:{k_buf} offset:{k_off_e} atIndex:3];\n"));
            src.push_str(&format!(
                "        {{ uint32_t _p[8] = {{seq_len, act_stride, {rope_dim_q}, {head_dim}, 0, {base}, total_seq_len, {rope_dim_k}}};\n\
                 \x20         [enc setBytes:_p length:32 atIndex:4]; }}\n"
            ));
            src.push_str("        [enc setBuffer:s->rope_table offset:0 atIndex:5];\n");
            src.push_str("        DISPATCH_BARRIER();\n");
            src.push_str("        [enc setComputePipelineState:pso_rope_qk_f16];\n");
            src.push_str("        [enc dispatchThreadgroups:MTLSizeMake(seq_len, 1, 1) threadsPerThreadgroup:MTLSizeMake(256, 1, 1)];\n");
            src.push_str("    } else {\n");
            {
                let (d_oq, d_oq_o) = resolve_buffer(node.output, graph, memory_plan, &act_index, true);
                let (d_q, d_q_o) = resolve_buffer(node.inputs[0], graph, memory_plan, &act_index, true);
                let (d_ok, d_ok_o) = resolve_buffer(k_node.output, graph, memory_plan, &act_index, true);
                let (d_k, d_k_o) = resolve_buffer(k_node.inputs[0], graph, memory_plan, &act_index, true);
                src.push_str(&format!("        [enc setBuffer:{d_oq} offset:{d_oq_o} atIndex:0];\n"));
                src.push_str(&format!("        [enc setBuffer:{d_q} offset:{d_q_o} atIndex:1];\n"));
                src.push_str(&format!("        [enc setBuffer:{d_ok} offset:{d_ok_o} atIndex:2];\n"));
                src.push_str(&format!("        [enc setBuffer:{d_k} offset:{d_k_o} atIndex:3];\n"));
            }
            src.push_str(&format!(
                "        {{ uint32_t _p[8] = {{seq_len, act_stride, {rope_dim_q}, {head_dim}, 0, {base}, total_seq_len, {rope_dim_k}}};\n\
                 \x20         [enc setBytes:_p length:32 atIndex:4]; }}\n"
            ));
            src.push_str("        [enc setBuffer:s->rope_table offset:0 atIndex:5];\n");
            src.push_str("        DISPATCH_BARRIER();\n");
            src.push_str("        [enc setComputePipelineState:pso_rope_qk_f16];\n");
            src.push_str("        [enc dispatchThreadgroups:MTLSizeMake(1, 1, 1) threadsPerThreadgroup:MTLSizeMake(256, 1, 1)];\n");
            src.push_str("    }\n\n");
            continue;
        }

        // Check for KVCacheAppend K+V fusion
        if let Some(fusion) = kv_append_at.get(&idx) {
            let v_node = &graph.nodes[fusion.v_idx];
            let p = &graph.metadata.params;

            // K node: inputs[0] = kv_cache, inputs[1] = new_k
            // V node: inputs[0] = kv_cache, inputs[1] = new_v
            // For kv_cache_append, resolve the KV cache buffers and new K/V buffers
            let (k_cache_buf, k_cache_off) = resolve_buffer(node.output, graph, memory_plan, &act_index, false);
            let (new_k_buf, new_k_off) = resolve_buffer(node.inputs[1], graph, memory_plan, &act_index, false);
            let (v_cache_buf, v_cache_off) = resolve_buffer(v_node.output, graph, memory_plan, &act_index, false);
            let (new_v_buf, new_v_off) = resolve_buffer(v_node.inputs[1], graph, memory_plan, &act_index, false);

            let k_cache_off_e = format_offset(&k_cache_buf, k_cache_off);
            let new_k_off_e = format_offset(&new_k_buf, new_k_off);
            let v_cache_off_e = format_offset(&v_cache_buf, v_cache_off);
            let new_v_off_e = format_offset(&new_v_buf, new_v_off);

            let head_dim = p.head_dim as u32;
            let max_seq = p.max_position_embeddings as u32;
            let kv_dim = (p.num_kv_heads * p.head_dim) as u32;

            src.push_str(&format!("    // Fused KVAppend K+V: {:?} + {:?}\n", node.id, v_node.id));
            src.push_str("    if (seq_len > 1) {\n");
            src.push_str(&format!("        [enc setBuffer:{k_cache_buf} offset:{k_cache_off_e} atIndex:0];\n"));
            src.push_str(&format!("        [enc setBuffer:{new_k_buf} offset:{new_k_off_e} atIndex:1];\n"));
            src.push_str(&format!("        [enc setBuffer:{v_cache_buf} offset:{v_cache_off_e} atIndex:2];\n"));
            src.push_str(&format!("        [enc setBuffer:{new_v_buf} offset:{new_v_off_e} atIndex:3];\n"));
            src.push_str(&format!(
                "        {{ uint32_t _p[8] = {{seq_len, total_seq_len, act_stride, {head_dim}, {max_seq}, {kv_dim}, 0, 0}};\n\
                 \x20         [enc setBytes:_p length:32 atIndex:4]; }}\n"
            ));
            src.push_str("        DISPATCH_BARRIER();\n");
            src.push_str("        [enc setComputePipelineState:pso_kv_cache_append_fused_f16];\n");
            src.push_str("        [enc dispatchThreadgroups:MTLSizeMake(seq_len, 1, 1) threadsPerThreadgroup:MTLSizeMake(256, 1, 1)];\n");
            src.push_str("    } else {\n");
            {
                let (d_nk, d_nk_o) = resolve_buffer(node.inputs[1], graph, memory_plan, &act_index, true);
                let (d_nv, d_nv_o) = resolve_buffer(v_node.inputs[1], graph, memory_plan, &act_index, true);
                src.push_str(&format!("        [enc setBuffer:{k_cache_buf} offset:{k_cache_off_e} atIndex:0];\n"));
                src.push_str(&format!("        [enc setBuffer:{d_nk} offset:{d_nk_o} atIndex:1];\n"));
                src.push_str(&format!("        [enc setBuffer:{v_cache_buf} offset:{v_cache_off_e} atIndex:2];\n"));
                src.push_str(&format!("        [enc setBuffer:{d_nv} offset:{d_nv_o} atIndex:3];\n"));
            }
            src.push_str(&format!(
                "        {{ uint32_t _p[8] = {{seq_len, total_seq_len, act_stride, {head_dim}, {max_seq}, {kv_dim}, 0, 0}};\n\
                 \x20         [enc setBytes:_p length:32 atIndex:4]; }}\n"
            ));
            src.push_str("        DISPATCH_BARRIER();\n");
            src.push_str("        [enc setComputePipelineState:pso_kv_cache_append_fused_f16];\n");
            src.push_str("        [enc dispatchThreadgroups:MTLSizeMake(1, 1, 1) threadsPerThreadgroup:MTLSizeMake(256, 1, 1)];\n");
            src.push_str("    }\n\n");
            continue;
        }

        // QKNorm: per-head RMSNorm with custom grid = seq_len * num_heads
        if let Op::QKNorm { eps, num_heads, head_dim } = &node.op {
            let eps_bits = (*eps as f32).to_bits();
            src.push_str(&format!("    // QKNorm {:?}: {} heads x {} head_dim\n", node.id, num_heads, head_dim));
            src.push_str("    if (seq_len > 1) {\n");
            src.push_str(&emit_buffer_bindings(node, graph, memory_plan, &act_index, "        "));
            src.push_str(&format!(
                "        {{ uint32_t _p[8] = {{seq_len, act_stride, {head_dim}, {eps_bits}, {num_heads}, 0, 0, 0}};\n\
                 \x20         [enc setBytes:_p length:32 atIndex:4]; }}\n"
            ));
            src.push_str("        DISPATCH_BARRIER();\n");
            src.push_str("        [enc setComputePipelineState:pso_qk_norm_f16];\n");
            src.push_str("        [enc setThreadgroupMemoryLength:512 atIndex:0];\n");
            src.push_str(&format!("        [enc dispatchThreadgroups:MTLSizeMake(seq_len * {num_heads}, 1, 1) threadsPerThreadgroup:MTLSizeMake(128, 1, 1)];\n"));
            src.push_str("    } else {\n");
            src.push_str(&emit_buffer_bindings_decode(node, graph, memory_plan, "        "));
            src.push_str(&format!(
                "        {{ uint32_t _p[8] = {{seq_len, act_stride, {head_dim}, {eps_bits}, {num_heads}, 0, 0, 0}};\n\
                 \x20         [enc setBytes:_p length:32 atIndex:4]; }}\n"
            ));
            src.push_str("        DISPATCH_BARRIER();\n");
            src.push_str("        [enc setComputePipelineState:pso_qk_norm_f16];\n");
            src.push_str("        [enc setThreadgroupMemoryLength:512 atIndex:0];\n");
            src.push_str(&format!("        [enc dispatchThreadgroups:MTLSizeMake({num_heads}, 1, 1) threadsPerThreadgroup:MTLSizeMake(128, 1, 1)];\n"));
            src.push_str("    }\n\n");
            continue;
        }

        let ka = match &node.kernel {
            Some(k) => k,
            None => continue,
        };

        match &ka.path {
            ExecutionPath::Unified | ExecutionPath::Prefill => {
                src.push_str(&format!("    // Node {:?}: {:?}\n", node.id, node.op));
                if matches!(ka.path, ExecutionPath::Unified) {
                    // Unified: split into prefill/decode for different buffer bindings
                    src.push_str("    if (seq_len > 1) {\n");
                    src.push_str(&emit_buffer_bindings(node, graph, memory_plan, &act_index, "        "));
                    src.push_str(&emit_op_params(node, graph, "        "));
                    src.push_str(&emit_dispatch_indented(&ka.kernel_name, &ka.dispatch, "seq_len", "        "));
                    src.push_str("    } else {\n");
                    // Check if this is an attention node → use MLX sdpa_vector for decode
                    if let Op::ScaledDotProductAttention { head_dim, gqa_factor, scale, .. } = &node.op {
                        if sdpa_fused_by_rope.contains(&idx) {
                            src.push_str("        // SDPA handled by fused RoPE+KV+SDPA kernel\n");
                        } else {
                        src.push_str(&emit_sdpa_vector_decode(
                            node, graph, memory_plan, *head_dim, *gqa_factor, *scale, &graph.metadata.params,
                        ));
                        }
                    } else {
                        src.push_str(&emit_buffer_bindings_decode(node, graph, memory_plan, "        "));
                        src.push_str(&emit_op_params(node, graph, "        "));
                        src.push_str(&emit_dispatch_indented(&ka.kernel_name, &ka.dispatch, "seq_len", "        "));
                    }
                    src.push_str("    }\n");
                } else {
                    // Prefill-only: just emit as before
                    src.push_str(&emit_buffer_bindings(node, graph, memory_plan, &act_index, "    "));
                    src.push_str(&emit_op_params(node, graph, "    "));
                    src.push_str(&emit_dispatch_indented(&ka.kernel_name, &ka.dispatch, "seq_len", "    "));
                }
                src.push_str("\n");
            }
            ExecutionPath::Dual {
                prefill_kernel_name,
                prefill_dispatch,
                decode_kernel_name,
                decode_dispatch,
                ..
            } => {
                let quant_active = matches!(quant, "q8_0" | "q4_0" | "q4_mlx");
                src.push_str(&format!("    // Node {:?}: {:?} (dual prefill/decode)\n", node.id, node.op));
                src.push_str("    if (seq_len > 1) {\n");
                src.push_str(&emit_buffer_bindings(node, graph, memory_plan, &act_index, "        "));
                src.push_str(&emit_op_params(node, graph, "        "));
                src.push_str(&emit_dispatch_indented(prefill_kernel_name, prefill_dispatch, "seq_len", "        "));
                src.push_str("    } else {\n");
                if quant_active && decode_kernel_name == "gemv_f16" {
                    // Check for PSQ writer: GEMV + residual add + partial sum-of-squares
                    if let Some(psq_w) = psq_writers.get(&idx) {
                        let p = &graph.metadata.params;
                        // C = add_output (new residual), A = activation, B = quant weight
                        let (c_buf, c_off) = resolve_buffer(psq_w.add_output_tid, graph, memory_plan, &act_index, true);
                        let act_in_fused_attn = fused_attn_tids.contains(&node.inputs[0]);
                        let (a_buf, a_off) = resolve_buffer(node.inputs[0], graph, memory_plan, &act_index, true);
                        let (res_buf, res_off) = resolve_buffer(psq_w.residual_tid, graph, memory_plan, &act_index, true);
                        let w_tid = node.inputs[1];
                        let tensor = graph.tensors.get(&w_tid).unwrap();
                        let (b_buf, b_off) = if let StorageClass::Weight(w) = &tensor.storage {
                            ("s->quant_weight_buf".to_string(), quant_map.get(&w.byte_offset).copied().unwrap_or(0))
                        } else {
                            let (b, o) = resolve_buffer(w_tid, graph, memory_plan, &act_index, true);
                            (b.to_string(), o)
                        };
                        let m_out = graph.tensors.get(&node.output)
                            .map(|t| t.shape.max_shape()).unwrap_or_default()
                            .first().copied().unwrap_or(p.hidden_size) as u32;
                        let k_dim = graph.tensors.get(&node.inputs[1])
                            .map(|t| t.shape.max_shape()).unwrap_or_default()
                            .last().copied().unwrap_or(p.hidden_size) as u32;
                        let grid_x = (m_out + 15) / 16;

                        src.push_str("        // PSQ writer: GEMV + Add + partial sum-of-squares\n");
                        src.push_str(&format!("        [enc setBuffer:{c_buf} offset:{} atIndex:0];\n", format_offset(&c_buf, c_off)));
                        if act_in_fused_attn {
                            src.push_str("        [enc setBuffer:s->fused_attn_buf offset:0 atIndex:1];\n");
                        } else {
                            src.push_str(&format!("        [enc setBuffer:{a_buf} offset:{} atIndex:1];\n", format_offset(&a_buf, a_off)));
                        }
                        src.push_str(&format!("        [enc setBuffer:{b_buf} offset:{b_off} atIndex:2];\n"));
                        src.push_str(&format!("        [enc setBuffer:{res_buf} offset:{} atIndex:3];\n", format_offset(&res_buf, res_off)));
                        src.push_str("        [enc setBuffer:s->psq_buf offset:0 atIndex:4];\n");
                        src.push_str(&format!(
                            "        {{ uint32_t _p[8] = {{1, act_stride, {k_dim}, {m_out}, 0, 0, 0, 0}};\n\
                             \x20         [enc setBytes:_p length:32 atIndex:5]; }}\n"
                        ));
                        src.push_str("        DISPATCH_BARRIER();\n");
                        src.push_str(&format!("        [enc setComputePipelineState:pso_qmv_add_psq_{}];\n", q4_suffix));
                        src.push_str("        [enc setThreadgroupMemoryLength:16 atIndex:0];\n");
                        src.push_str(&format!("        [enc dispatchThreadgroups:MTLSizeMake({grid_x}, 1, 1) \
                             threadsPerThreadgroup:MTLSizeMake(128, 1, 1)];\n"));
                    } else {
                        src.push_str(&emit_buffer_bindings_quant_decode(node, graph, memory_plan, quant_map, "        "));
                        src.push_str(&emit_op_params(node, graph, "        "));
                        let m_out = graph.tensors.get(&node.output)
                            .map(|t| t.shape.max_shape())
                            .unwrap_or_default()
                            .first().copied().unwrap_or(graph.metadata.params.hidden_size) as u32;
                        if quant == "q4_0" {
                            // MLX QMV: 2 simdgroups × 4 rows = 8 rows/TG, 64 threads
                            let grid_y = (m_out + 7) / 8;
                            src.push_str("        DISPATCH_BARRIER();\n");
                            src.push_str("        [enc setComputePipelineState:pso_unc_mlx_qmv_fast_q4_0];\n");
                            src.push_str(&format!("        [enc dispatchThreadgroups:MTLSizeMake(1, {grid_y}, 1) \
                                 threadsPerThreadgroup:MTLSizeMake(32, 2, 1)];\n"));
                        } else if quant == "q4_mlx" {
                            // Q4_MLX standalone QMV: 4 simdgroups × 4 rows = 16 rows/TG, 128 threads
                            let grid_x = (m_out + 15) / 16;
                            src.push_str("        DISPATCH_BARRIER();\n");
                            src.push_str("        [enc setComputePipelineState:pso_qmv_fast_q4_mlx];\n");
                            src.push_str(&format!("        [enc dispatchThreadgroups:MTLSizeMake({grid_x}, 1, 1) \
                                 threadsPerThreadgroup:MTLSizeMake(128, 1, 1)];\n"));
                        } else {
                            // Fallback: original UNC kernel
                            let grid_x = (m_out + 15) / 16;
                            let q_kernel = format!("qmv_fast_{}", quant);
                            let q_pso_var = pso_var_name(&q_kernel);
                            src.push_str("        DISPATCH_BARRIER();\n");
                            src.push_str(&format!("        [enc setComputePipelineState:{q_pso_var}];\n"));
                            src.push_str(&format!("        [enc dispatchThreadgroups:MTLSizeMake({grid_x}, 1, 1) \
                                 threadsPerThreadgroup:MTLSizeMake(128, 1, 1)];\n"));
                        }
                    }
                } else {
                    src.push_str(&emit_buffer_bindings_decode(node, graph, memory_plan, "        "));
                    src.push_str(&emit_op_params(node, graph, "        "));
                    src.push_str(&emit_dispatch_indented(decode_kernel_name, decode_dispatch, "seq_len", "        "));
                }
                src.push_str("    }\n\n");
            }
            ExecutionPath::Decode => {
                src.push_str(&format!("    // Node {:?}: {:?}\n", node.id, node.op));
                src.push_str(&emit_buffer_bindings_decode(node, graph, memory_plan, "    "));
                src.push_str(&emit_op_params(node, graph, "    "));
                src.push_str(&emit_dispatch_indented(&ka.kernel_name, &ka.dispatch, "seq_len", "    "));
                src.push_str("\n");
            }
        }
    }

    // Find logit tensor region for decode
    let logit_region = {
        let logit_id = graph.outputs.first()
            .or_else(|| graph.nodes.last().map(|n| &n.output));
        logit_id.and_then(|id| memory_plan.decode_tensor_region.get(id)).copied().unwrap_or(0)
    };

    src.push_str(&format!(
        r#"
    // Logit gather on GPU: [vocab, act_stride] f16 → f32 output buffer
    {{
        uint32_t last_si = (seq_len > 0) ? seq_len - 1 : 0;
        uint32_t lg_params[3] = {{ UNC_VOCAB_SIZE, act_stride, last_si }};
        DISPATCH_BARRIER();
        [enc setComputePipelineState:pso_logit_gather_f32];
        [enc setBuffer:s->logit_buf offset:0 atIndex:0];
        if (seq_len > 1) {{
            [enc setBuffer:cur_act offset:UNC_LOGIT_OFFSET atIndex:1];
        }} else {{
            [enc setBuffer:s->act_r[{logit_region}] offset:0 atIndex:1];
        }}
        [enc setBytes:lg_params length:12 atIndex:2];
        [enc dispatchThreadgroups:MTLSizeMake((UNC_VOCAB_SIZE + 255) / 256, 1, 1)
                threadsPerThreadgroup:MTLSizeMake(256, 1, 1)];
    }}

    [enc endEncoding];

    struct timespec _ts1; clock_gettime(CLOCK_MONOTONIC_RAW, &_ts1);
    double _encode_ms = (_ts1.tv_sec - _ts0.tv_sec) * 1000.0 + (_ts1.tv_nsec - _ts0.tv_nsec) / 1e6;

    [cmd commit];
    [cmd waitUntilCompleted];

    struct timespec _ts2; clock_gettime(CLOCK_MONOTONIC_RAW, &_ts2);
    double _gpu_ms = (_ts2.tv_sec - _ts1.tv_sec) * 1000.0 + (_ts2.tv_nsec - _ts1.tv_nsec) / 1e6;

    _encode_total += _encode_ms;
    _gpu_total += _gpu_ms;
    _profile_count++;
    if (_profile_count == 50) {{
        NSLog(@"PROFILE: encode=%.2fms/tok gpu=%.2fms/tok total=%.2fms/tok (avg over %llu tokens)",
              _encode_total / _profile_count, _gpu_total / _profile_count,
              (_encode_total + _gpu_total) / _profile_count, _profile_count);
        _profile_count = 0; _encode_total = 0; _gpu_total = 0;
    }}

    // Copy GPU logit buffer → caller f32 array
    memcpy(out_logits, [s->logit_buf contents], UNC_VOCAB_SIZE * sizeof(float));
}}
"#,
    ));

    src
}

